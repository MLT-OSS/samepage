import org.apache.commons.logging.LogFactory;
import org.jetbrains.annotations.NotNull;
import org.springframework.beans.BeansException;
import org.springframework.beans.factory.InitializingBean;
import org.springframework.beans.factory.ObjectProvider;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.beans.factory.config.BeanPostProcessor;
import org.springframework.boot.autoconfigure.condition.ConditionalOnProperty;
import org.springframework.boot.autoconfigure.session.RedisSessionProperties;
import org.springframework.boot.autoconfigure.session.SessionProperties;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.data.redis.connection.RedisConnection;
import org.springframework.data.redis.connection.RedisConnectionFactory;
import org.springframework.data.redis.connection.jedis.JedisConnectionFactory;
import org.springframework.data.redis.connection.lettuce.LettuceConnectionFactory;
import org.springframework.data.redis.core.RedisTemplate;
import org.springframework.data.redis.serializer.Jackson2JsonRedisSerializer;
import org.springframework.data.redis.serializer.RedisSerializer;
import org.springframework.data.redis.serializer.StringRedisSerializer;
import org.springframework.session.*;
import org.springframework.session.config.SessionRepositoryCustomizer;
import org.springframework.session.data.redis.RedisIndexedSessionRepository;
import org.springframework.session.data.redis.config.ConfigureNotifyKeyspaceEventsAction;
import org.springframework.session.data.redis.config.ConfigureRedisAction;
import org.springframework.session.data.redis.config.annotation.web.http.EnableRedisHttpSession;
import org.springframework.session.web.http.CookieSerializer;
import org.springframework.util.StringUtils;

import javax.annotation.Resource;
import java.util.List;
import java.util.Objects;
import java.util.stream.Collectors;

/**
 * 初始化session 的配置信息
 *
 **/
@Configuration
@EnableRedisHttpSession(maxInactiveIntervalInSeconds = 7200)
@ConditionalOnProperty(prefix = "login.session", name = "active")
public class SessionConfig implements BeanPostProcessor {

    public static final String MJSESSIONID = "XM_SESSION_ID";


    public static final String CHECK_REDIS_BEAN_NAME = "enableRedisKeyspaceNotificationsInitializer";


    private IndexResolver<Session> indexResolver;

    private RedisSessionProperties redisSessionProperties;


    private SessionProperties sessionProperties;


    @Resource
    private RedisConnectionFactory redisConnectionFactory;
    // private ConfigureRedisAction configureRedisAction = new ConfigureNotifyKeyspaceEventsAction();
    private ConfigureRedisAction configureRedisAction = new ConfigureNotifyKeyspaceEventsAction();
    private List<SessionRepositoryCustomizer<RedisIndexedSessionRepository>> sessionRepositoryCustomizers;

    @Autowired(required = false)
    public void setSessionRepositoryCustomizer(
            ObjectProvider<SessionRepositoryCustomizer<RedisIndexedSessionRepository>> sessionRepositoryCustomizers) {
        this.sessionRepositoryCustomizers = sessionRepositoryCustomizers.orderedStream().collect(Collectors.toList());
    }


    @Autowired(required = false)
    public void setConfigureRedisAction(ConfigureRedisAction configureRedisAction) {
        this.configureRedisAction = configureRedisAction;
    }


    @Autowired(required = false)
    public void setIndexResolver(IndexResolver<Session> indexResolver) {
        this.indexResolver = indexResolver;
    }

    @Autowired(required = false)
    public void setRedisSessionProperties(RedisSessionProperties redisSessionProperties) {
        this.redisSessionProperties = redisSessionProperties;
    }

    @Autowired(required = false)
    public void setSessionProperties(SessionProperties sessionProperties) {
        this.sessionProperties = sessionProperties;
    }

    /**
     * 获取数据库信息
     *
     * @return 数据库编号
     */

    private int resolveDatabase() {
        if (this.redisConnectionFactory instanceof LettuceConnectionFactory) {
            return ((LettuceConnectionFactory) this.redisConnectionFactory).getDatabase();
        }
        if (this.redisConnectionFactory instanceof JedisConnectionFactory) {
            return ((JedisConnectionFactory) this.redisConnectionFactory).getDatabase();
        }
        return RedisIndexedSessionRepository.DEFAULT_DATABASE;
    }

    /**
     * 此配置可以关闭代码设置redis config的逻辑
     *
     * @return
     */
    @Bean
    public static ConfigureRedisAction configureRedisAction() {
        return ConfigureRedisAction.NO_OP;
    }

    @Override
    public Object postProcessBeforeInitialization(@NotNull Object bean, @NotNull String beanName) throws BeansException {
        // 校验 redis 连接信息
        if (bean instanceof InitializingBean && Objects.equals(CHECK_REDIS_BEAN_NAME, beanName)) {
            if (Objects.isNull(this.redisConnectionFactory)) {
                throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "初始化session 失败");
                //this.redisConnectionFactory = getRedisConnectionFactory();
            }
            return new EnableRedisKeyspaceNotificationsInitializer(redisConnectionFactory, this.configureRedisAction);
        }
        // 初始化 session repository
        if (bean instanceof SessionRepository) {
            if (Objects.isNull(this.redisConnectionFactory)) {
                throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "初始化session 失败");
                //this.redisConnectionFactory = getRedisConnectionFactory();
            }
            RedisIndexedSessionRepository redisIndexedSessionRepository = new RedisIndexedSessionRepository(createRedisTemplate());
            redisIndexedSessionRepository.setDatabase(resolveDatabase());
            if (this.indexResolver != null) {
                redisIndexedSessionRepository.setIndexResolver(this.indexResolver);
            }
            if (Objects.nonNull(sessionProperties) && Objects.nonNull(sessionProperties.getTimeout())) {
                redisIndexedSessionRepository.setDefaultMaxInactiveInterval((int) sessionProperties.getTimeout().getSeconds());
            } else {
                redisIndexedSessionRepository.setDefaultMaxInactiveInterval(MapSession.DEFAULT_MAX_INACTIVE_INTERVAL_SECONDS);
            }
            if (Objects.nonNull(redisSessionProperties) && StringUtils.hasText(redisSessionProperties.getNamespace())) {
                redisIndexedSessionRepository.setRedisKeyNamespace(redisSessionProperties.getNamespace());
            } else {
                redisIndexedSessionRepository.setRedisKeyNamespace(RedisIndexedSessionRepository.DEFAULT_NAMESPACE);
            }
            if (Objects.nonNull(redisSessionProperties) && Objects.nonNull(redisSessionProperties.getFlushMode())) {
                redisIndexedSessionRepository.setFlushMode(redisSessionProperties.getFlushMode());
            } else {
                redisIndexedSessionRepository.setFlushMode(FlushMode.ON_SAVE);
            }
            if (Objects.nonNull(redisSessionProperties) && Objects.nonNull(redisSessionProperties.getSaveMode())) {
                redisIndexedSessionRepository.setSaveMode(redisSessionProperties.getSaveMode());
            } else {
                redisIndexedSessionRepository.setSaveMode(SaveMode.ON_SET_ATTRIBUTE);
            }
            redisIndexedSessionRepository.setDefaultSerializer(defaultRedisSerializer());
            this.sessionRepositoryCustomizers
                    .forEach((sessionRepositoryCustomizer) -> sessionRepositoryCustomizer.customize(redisIndexedSessionRepository));
            return redisIndexedSessionRepository;
        }
        return bean;
    }


    @Value("${login.session.domainPattern:(.*)}")
    private String domainPattern;

    @Value("${login.session.maxInterval: 3600}")
    private Integer activeInterval;

    @Value("${login.session.multiDomain: false}")
    private Boolean multiDomain;

    //@Value("${login.session.maxAge:3600}")
    //private Integer maxAge;
    @Bean
    @ConditionalOnProperty(prefix = "login.session", name = "active")
    public CookieSerializer defaultCookieSerializer() {
        CustomCookieSerializer defaultCookieSerializer = new CustomCookieSerializer();
        defaultCookieSerializer.setCookiePath("/");
        if (StrUtil.isNotBlank(domainPattern)){
            defaultCookieSerializer.setDomainNamePattern(domainPattern);
        }else {
            defaultCookieSerializer.setDomainName(".");
        }
        defaultCookieSerializer.setCookieName(MJSESSIONID);
        defaultCookieSerializer.setCookieMaxAge(activeInterval);
        defaultCookieSerializer.setUseMultiDomain(multiDomain);
        return defaultCookieSerializer;
    }


    private RedisTemplate<Object, Object> createRedisTemplate() {
        RedisTemplate<Object, Object> redisTemplate = new RedisTemplate<>();
        redisTemplate.setKeySerializer(new StringRedisSerializer());
        redisTemplate.setHashKeySerializer(new StringRedisSerializer());
        redisTemplate.setDefaultSerializer(defaultRedisSerializer());
        redisTemplate.setConnectionFactory(this.redisConnectionFactory);
        redisTemplate.setBeanClassLoader(this.getClass().getClassLoader());
        redisTemplate.afterPropertiesSet();
        return redisTemplate;
    }

    static class EnableRedisKeyspaceNotificationsInitializer implements InitializingBean {

        private final RedisConnectionFactory connectionFactory;

        private final ConfigureRedisAction configure;

        EnableRedisKeyspaceNotificationsInitializer(RedisConnectionFactory connectionFactory,
                                                    ConfigureRedisAction configure) {
            this.connectionFactory = connectionFactory;
            this.configure = configure;
        }

        @Override
        public void afterPropertiesSet() {
            if (this.configure == ConfigureRedisAction.NO_OP) {
                return;
            }
            RedisConnection connection = this.connectionFactory.getConnection();
            try {
                this.configure.configure(connection);
            } finally {
                try {
                    connection.close();
                } catch (Exception ex) {
                    LogFactory.getLog(getClass()).error("Error closing RedisConnection", ex);
                }
            }
        }

    }


    /**
     * session 默认序列化信息
     *
     * @return 默认序列化
     */
    public RedisSerializer<Object> defaultRedisSerializer() {
        return new Jackson2JsonRedisSerializer<>(Object.class);
    }

}
