package ai.samepage.openapi;

import ai.samepage.common.core.exception.BusinessException;
import ai.samepage.common.core.resp.RespCode;
import ai.samepage.common.core.util.JacksonUtils;
import ai.samepage.entity.DocumentTypeEnum;
import cn.hutool.core.util.StrUtil;
import io.reactivex.rxjava3.core.BackpressureStrategy;
import io.reactivex.rxjava3.core.Flowable;
import lombok.extern.slf4j.Slf4j;
import okhttp3.OkHttpClient;
import okhttp3.ResponseBody;
import retrofit2.*;
import retrofit2.adapter.rxjava3.RxJava3CallAdapterFactory;
import retrofit2.converter.jackson.JacksonConverterFactory;

import java.io.BufferedReader;
import java.io.InputStream;
import java.io.InputStreamReader;
import java.util.Collections;
import java.util.Objects;
import java.util.function.Consumer;


@Slf4j
public class IndexApiService {

    private final RemoteIndexApi remoteIndexApi;

    public IndexApiService(String baseUrl, OkHttpClient okHttpClient) {
        this.remoteIndexApi = new Retrofit.Builder()
                .baseUrl(baseUrl)
                .client(okHttpClient)
                .addConverterFactory(JacksonConverterFactory.create(
                        JacksonUtils.getObjectMapper()
                )).addCallAdapterFactory(RxJava3CallAdapterFactory.create())
                .build().create(RemoteIndexApi.class);
    }

    /**
     * 总结全文
     *
     * @param lang 语言
     * @param text 全文
     * @return
     */
    @Deprecated
    public Flowable<TextResponse> summary(String lang, String text) {
        log.info("摘要的传参是：lang:{},content:{}", lang, text);
        ArticleRequestBody articleRequestBody = new ArticleRequestBody();
        articleRequestBody.setLang(StrUtil.isBlank(lang) ? "chinese" : lang.toLowerCase());
        articleRequestBody.setDocumentText(text);
        return stream(remoteIndexApi.summary(articleRequestBody));

    }


    /**
     * qa 信息
     *
     * @param question    问题
     * @param lang        语言
     * @param docId       docId
     * @return 问问题
     */
    @Deprecated
    public Flowable<TextResponse> question(String question,
                                           String lang,
                                           String model,
                                           String docId) {
        if (StrUtil.isBlank(docId) || StrUtil.isBlank(question)) {
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "qa 的参数有问题");
        }
        log.info("qa的传参是：lang:{},doc:{},content:{}", lang, docId, question);
        QuestionRequestBody questionRequestBody = new QuestionRequestBody();
        questionRequestBody.setQuestion(question);
       /* List<QuestionRequestBody.HistoryDTO> history = chatMessage.stream().map(k -> {
            QuestionRequestBody.HistoryDTO historyDTO = new QuestionRequestBody.HistoryDTO();
            historyDTO.setRole(k.getRole());
            historyDTO.setContent(k.getContent());
            return historyDTO;
        }).collect(Collectors.toList());*/
        //questionRequestBody.setHistory(history);
        questionRequestBody.setHistory(Collections.emptyList());
        questionRequestBody.setDocumentId(docId);
        questionRequestBody.setChatModel(model);
        questionRequestBody.setLang(StrUtil.isBlank(lang) ? "chinese" : lang.toLowerCase());
        return stream(remoteIndexApi.questions(questionRequestBody));
    }

    private Flowable<TextResponse> stream(Call<ResponseBody> api) {
        return Flowable.create(
                emitter -> api.enqueue(new Callback<ResponseBody>() {
                    @Override
                    public void onResponse(Call<ResponseBody> call, Response<ResponseBody> response) {
                        if (!response.isSuccessful()) {
                            this.onFailure(call, new HttpException(response));
                        } else {
                            try {
                                InputStream inputStream = response.body().byteStream();
                                BufferedReader bufferedReader = new BufferedReader(new InputStreamReader(inputStream), 2048);
                                String line;
                                while ((line = bufferedReader.readLine()) != null) {
                                    emitter.onNext(JacksonUtils.readValue(line, TextResponse.class));
                                }
                                emitter.onComplete();
                            } catch (Exception e) {
                                log.error(e.getMessage(), e);
                                onFailure(call, e);
                            }
                        }
                    }

                    @Override
                    public void onFailure(Call<ResponseBody> call, Throwable t) {
                        log.error(t.getMessage(), t);
                        emitter.onError(t);
                        emitter.onComplete();
                    }
                })
                , BackpressureStrategy.BUFFER);
    }

    /**
     * 获取任务列表
     *
     * @param taskInfo 任务的基本信息
     * @return 任务信息
     */
    public TaskResponse pushTask(TaskInfo taskInfo) {
        try {
            if (log.isInfoEnabled()) {
                log.info("传参是：{}", JacksonUtils.writeValueAsString(taskInfo));
            }
            return remoteIndexApi.pushTask(taskInfo).blockingGet();
        } catch (Throwable throwable) {
            log.error(throwable.getMessage(), throwable);
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, StrUtil.format("提交任务失败：失败原因：{}",
                    throwable.getMessage()));
        }
    }


    /**
     * 获取任务的进度
     *
     * @param taskId 进度id
     * @return 状态信息
     */
    public ProgressResponse getTaskInfo(String taskId) {
        return remoteIndexApi.getTaskInfo(taskId).blockingGet();
    }


    /**
     * @param taskId
     * @return
     */
    public Flowable<TextResponse> summary(String taskId) {
        if (log.isInfoEnabled()) {
            log.info("查询的任务id:{}", taskId);
        }
        return stream(remoteIndexApi.streamingTask(taskId));
    }


    public void retryDocument(String documentId, String lan,
                              DocumentTypeEnum documentType,
                              Consumer<TextResponse> consumer) {
        String type = Objects.isNull(documentType) ? "text" : documentType.getName();
        QuestionRequestBody questionRequestBody = new QuestionRequestBody();
        questionRequestBody.setDocumentId(documentId);
        questionRequestBody.setLang(lan);
        questionRequestBody.setDocumentType(type);
        Flowable<TextResponse> stream = stream(remoteIndexApi
                .streamingRetryDoc(questionRequestBody));
        for (TextResponse textResponse : stream.blockingIterable()) {
            consumer.accept(textResponse);
        }

    }

    public void questionDoc(String documentId, String language,
                            String model,
                            String question, DocumentTypeEnum documentType,
                            Consumer<TextResponse> consumer) {
        log.info("qa的传参是：lang:{},doc:{},content:{}", language, documentId, question);
        QuestionRequestBody questionRequestBody = new QuestionRequestBody();
        questionRequestBody.setQuestion(question);
        questionRequestBody.setHistory(Collections.emptyList());
        questionRequestBody.setDocumentId(documentId);
        questionRequestBody.setChatModel(model);
        questionRequestBody.setLang(StrUtil.isBlank(language) ? "chinese" : language);
        questionRequestBody.setDocumentType(Objects.isNull(documentType) ? "text" : documentType.getName());
        Flowable<TextResponse> stream = stream(remoteIndexApi.questions(questionRequestBody));
        for (TextResponse textResponse : stream.blockingIterable()) {
            consumer.accept(textResponse);
        }
    }


    public void chat(QuestionRequestBody requestBody, Consumer<TextResponse> consumer) {
        Flowable<TextResponse> stream = stream(remoteIndexApi.questions(requestBody));
        for (TextResponse textResponse : stream.blockingIterable()) {
            consumer.accept(textResponse);
        }
    }
}
