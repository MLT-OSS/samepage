package ai.samepage.service;


import ai.samepage.entity.BotTypeEnum;
import ai.samepage.entity.ConversationMessage;
import ai.samepage.model.vo.ConversationMsgVO;
import ai.samepage.model.vo.MessageChatVO;
import ai.samepage.model.vo.UploadObjConfigVO;
import ai.samepage.entity.BotInfo;
import ai.samepage.model.MessageEnum;

import java.util.List;

/**
 * 会话消息
 *
 */
public interface ConvMsgBotService {

    /**
     * 修改一个会话某条消息点赞
     *
     * @param key    机器人唯一标识
     * @param corpId
     * @param userId 用户id
     * @param convId 会话id
     * @param msgId  消息id
     * @param like   点赞
     */
    void convMsgFeedback(String key, Integer corpId, Long userId, Long convId, String msgId, Boolean like);

    /**
     * 获取会话的历史记录
     *
     * @param type   机器人类型
     * @param key    机器人的唯一标识
     * @param corpId
     * @param userId 用户id
     * @param convId 会话id
     * @param all    是否所有的消息
     * @param domain 前端访问的地址
     * @return
     */
    ConversationMsgVO listMessage(BotTypeEnum type,
                                  String key,
                                  Integer corpId,
                                  Long userId,
                                  long convId, Boolean all, String domain);

    /**
     * 批量获取消息的基本信息
     *
     * @param bot    小机器人
     * @param userId 用户id
     * @param convId 会话id
     * @param msgIds 消息id列表
     * @return
     */
    List<ConversationMessage> listMessage(
            BotInfo bot, Long userId, Long convId, String... msgIds
    );

    /**
     * 删除 消息
     *
     * @param key       机器人唯一标识
     * @param corpId
     * @param userId    用户id
     * @param convId    会话id
     * @param messageId 消息id
     * @return
     */
    Boolean deleteMsg(String key, Integer corpId, Long userId, long convId, String messageId);

    /**
     * 获取会话消息
     *
     * @param key    机器人的key
     * @param corpId
     * @param userId 用户id
     * @param convId 会话id
     * @param msgId  消息id
     * @param domain
     * @return
     */
    MessageChatVO getMsg(String key, Integer corpId, Long userId, Long convId, String msgId, String domain);


    /**
     * 获取 消费的消息
     *
     * @param botInfo 机器人的类型
     * @param userId  用户id
     * @param convId  会话id
     * @param msgId   消息的id
     * @param domain
     * @return 消费的消息
     */
    MessageChatVO getMsg(BotInfo botInfo, Long userId, Long convId, String msgId, String domain);

    /**
     * 保存 消息的内容
     *
     * @param batch
     */
    void saveBatch(BotInfo bot, List<ConversationMessage> batch);

    /**
     * 修改消息的状态
     *
     * @param bot
     * @param convId      会话id
     * @param errorStatus 状态
     * @param messageIds  消息的id
     */
    void updateStatus(BotInfo bot, Long convId, Integer errorStatus, List<String> messageIds);


    /**
     * 获取消息信息
     *
     * @param bot    机器人
     * @param convId 会话id
     * @param userId
     * @return
     */
    List<ConversationMessage> listMsg(
            BotInfo bot, Long convId, Long userId
    );

    /**
     * 更新消息信息
     *
     * @param botInfo 机器人信息
     * @param convMsg 会话消息
     */
    void update(BotInfo botInfo, ConversationMessage convMsg, MessageEnum... statusCode);

    /**
     * 获取模板的消息
     *
     * @param botInfo 机器人
     * @param convId  会话id
     * @param userId  用户id
     * @param content 模版参数
     * @return
     */
    ConversationMessage getTemplateMsg(BotInfo botInfo, Long convId, Long userId, String content);


    ConversationMessage getTemplateMsgById(BotInfo botInfo, Long convId, Long userId, String messageId);


    /**
     * 转换消息信息
     *
     * @param msg     消息信息
     * @param botInfo
     * @param domain
     * @return
     */
    MessageChatVO toMsgChat(ConversationMessage msg, BotInfo botInfo, String domain);


    /**
     * 转换message 信息
     *
     * @param messageChat message 信息
     * @return
     */
    ConversationMessage toConvMsg(MessageChatVO messageChat);

    /**
     * 停止消息信息
     *
     * @param botInfo 机器人列表
     * @param userId
     * @param message 消费消息
     * @param domain
     */
    MessageChatVO stopMsg(BotInfo botInfo, Long userId, MessageChatVO message, String domain);


    /**
     * 获取可上传的地址
     *
     * @param type   类型
     * @param md5    文件的md5
     * @param suffix
     * @return
     */
    UploadObjConfigVO getUploadConfig(String type, String md5, String suffix);
}
