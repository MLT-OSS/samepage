    @Override
    @Transactional(rollbackFor = Exception.class)
    public TaskInfoVO createTask(String text,
                                 Long userId,
                                 String docId,
                                 ChatLanguageEnum language,
                                 String languageLocale) {
        if (StrUtil.isBlank(text) && StrUtil.isBlank(docId)) {
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR,
                    "获取不到任务的内容信息");
        }
        if (Objects.isNull(language)) {
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "获取不到任务的语言设置");
        }
        Locale locale = language.getLocale();
        if (Objects.isNull(locale)) {
            locale = SessionUtil.getLocale(languageLocale);
        }
        String lang = locale.getDisplayLanguage(Locale.ENGLISH);
        DocumentInfo documentInfo = null;
        TaskInfo taskInfo = new TaskInfo();
        TaskInfoVO taskInfoVO = new TaskInfoVO();
        taskInfoVO.setLanguage(lang);
        taskInfoVO.setCreateId(userId);
        taskInfoVO.setCreateTime(LocalDateTime.now());
        if (StrUtil.isNotBlank(text)) {
            taskInfo.setDocumentText(text);
            taskInfo.setDocumentType(DocumentTypeEnum.TEXT.getName());
            taskInfoVO.setDocType(DocumentTypeEnum.TEXT);
        } else {
            documentInfo = documentInfoService.getInfo(Long.parseLong(docId), userId);
            String md5 = documentInfo.getMd5();
            if (StrUtil.isBlank(md5)) {
                throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "获取不到文件的基本信息");
            }
            documentInfo.setStatus(DocumentStatusEnum.RUNNING);
            taskInfoVO.setDocId(String.valueOf(documentInfo.getId()));
            taskInfoVO.setDocType(documentInfo.getDocType());
            taskInfoVO.setName(documentInfo.getDocName());
            taskInfoVO.setSize(documentInfo.getSize());
            taskInfoVO.setMd5(md5);
            DocumentSummary documentSummary = documentSummaryService.getOne(
                    Wrappers.lambdaQuery(DocumentSummary.class)
                            .eq(DocumentSummary::getMd5, md5)
                            .eq(DocumentSummary::getLanguage, lang)
            );
            if (Objects.nonNull(documentSummary)) {
                String taskId = StrUtil.concat(true, DOC_CACHE_TASK_ID_PREFIX,
                        userId.toString(),
                        ":", IdUtil.fastUUID());
                taskInfoVO.setTaskId(taskId);
                taskInfoVO.setComplete(true);
                taskInfoVO.setProgress(100);
                taskInfoVO.setTotalProgress(100);
                taskInfoVO.setDocumentId(documentSummary.getDocumentId());
                saveTaskInfo(taskInfoVO);
                documentInfo.setStatus(DocumentStatusEnum.HANDLER_SUCCESS);
                documentInfo.setDocumentId(documentSummary.getDocumentId());
                documentInfoService.save(documentInfo);
                return taskInfoVO;
            }
            DocumentTypeEnum docType = documentInfo.getDocType();
            taskInfo.setDocumentType(docType.getName());
            // 构建下载配置，下载链接的有效期是1天
            taskInfo.setDocumentUrl(internalDocumentS3Service.downloadPresignedUrl(
                    documentInfo.getObjId(),
                    86400,120
            ).toString());
        }
        taskInfo.setLang(lang);
        TaskResponse taskResponse = indexApiService.pushTask(taskInfo);
        String id = taskResponse.getTaskId();
        taskInfoVO.setId(id);
        taskInfoVO.setTaskId(getTaskId(text, userId));
        String documentId = taskResponse.getDocumentId();
        taskInfoVO.setDocumentId(documentId);
        taskInfoVO.setComplete(false);
        saveTaskInfo(taskInfoVO);
        if (StrUtil.isBlank(text) && Objects.nonNull(documentInfo)) {
            documentInfoService.save(documentInfo);
            redisTemplate.delete(StrUtil.format(CacheKeyConstants.UPLOAD_INFO_KEY, userId, docId));
        }
        return taskInfoVO;
    }


    /**
     * 缓存task的基本信息
     *
     * @param taskInfo task 的基本信息
     * @return
     */
    private void saveTaskInfo(TaskInfoVO taskInfo) {
        redisTemplate.opsForValue().set(
                StrUtil.format("conversation:task:info:{}", taskInfo.getTaskId()),
                taskInfo,
                CacheKeyConstants.getExpireTimeSecond(7200), TimeUnit.SECONDS
        );

    }

    private TaskInfoVO getTask(String taskId) {
        TaskInfoVO taskInfoVO = redisTemplate.opsForValue().get(
                StrUtil.format("conversation:task:info:{}", taskId)
        );
        if (Objects.isNull(taskInfoVO)) {
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "任务不存在或者已删除");
        }
        if (StrUtil.isNotBlank(taskInfoVO.getError())) {
            log.error("任务出现{}错误", taskId);
            deleteTaskInfo(taskId);
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, taskInfoVO.getError());
        }
        return taskInfoVO;
    }

    private void deleteTaskInfo(String taskId) {
        redisTemplate.delete(StrUtil.format("conversation:task:info:{}", taskId));
    }

    @Override
    public void cancelTask(String taskId) {
        TaskInfoVO taskInfo = getTaskInfo(taskId);
        deleteTaskInfo(taskId);
        String docId = taskInfo.getDocId();
        if (StrUtil.isNotBlank(docId)) {
            documentInfoService.remove(Wrappers
                    .lambdaQuery(DocumentInfo.class)
                    .eq(DocumentInfo::getId, Long.parseLong(taskInfo.getDocId()))
            );
        }
    }

    @Override
    public TaskInfoVO getTaskInfo(String taskId) {
        TaskInfoVO task = getTask(taskId);
        if (taskId.startsWith(DOC_CACHE_TASK_ID_PREFIX)) {
            return task;
        }
        String id = task.getId();
        if (StrUtil.isBlank(id)) {
            log.error("执行任务的taskId:{}获取不到", taskId);
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR,
                    "执行任务的taskId获取不到");
        }
        if (task.getComplete()) {
            String docId = task.getDocId();
            if (StrUtil.isNotBlank(docId)) {
                documentInfoService.update(
                        Wrappers.lambdaUpdate(DocumentInfo.class)
                                .eq(DocumentInfo::getId,
                                        Long.parseLong(task.getDocId()))
                                .eq(DocumentInfo::getStatus, DocumentStatusEnum.RUNNING)
                                .set(DocumentInfo::getStatus, DocumentStatusEnum.HANDLER_SUCCESS)

                );
            }
            return task;
        }
        ProgressResponse progressResponse = indexApiService.getTaskInfo(id);
        String status = progressResponse.getStatus();
        if (Objects.equals(status, "error")) {
            String message = "处理失败，请重新点击\"阅读此文章\"进行重试";
            String docId = task.getDocId();
            RespCode respCode = RespCode.INTERNAL_SERVER_ERROR;
            if (StrUtil.isNotBlank(docId)) {
                message = "处理失败，请重新上传PDF文件进行重试";
            }
            String code = progressResponse.getCode();
            if (Objects.equals(code, "content_moderation_block")) {
                if (StrUtil.isNotBlank(docId)) {
                    message = "PDF文件内容涉及政治、色情、暴力等敏感内容，请检查后重新上传";
                } else {
                    message = "文章内容涉及敏感内容，阅读失败";
                }
                respCode = RespCode.CONTENT_AUDIT_FAILED;
            } else if (Objects.equals(code, "document_content_too_long")) {
                if (StrUtil.isNotBlank(docId)) {
                    message = "您上传的PDF文件所含字数过多，请进行删减或分批上传";
                } else {
                    message = "网页文本字数过多，阅读页面失败";
                }
                respCode = RespCode.CONTENT_AUDIT_FAILED;
            } else if (Objects.equals(code, "document_content_empty")) {
                if (StrUtil.isNotBlank(docId)) {
                    message = "未识别到PDF内文字信息";
                } else {
                    message = "未识别到网页内文字信息";
                }
                respCode = RespCode.CONTENT_AUDIT_FAILED;

            }
            //saveTaskInfo(task);
            log.error("任务：{}:{}:出现错误：{}",
                    taskId, id, progressResponse.getMessage());
            deleteTaskInfo(taskId);
            throw new BusinessException(respCode,
                    message);
        }
        if (log.isInfoEnabled()) {
            log.info("进度信息：{}", JacksonUtils.writeValueAsString(progressResponse));
        }
        // 阅读，解析
        if (Objects.equals(status, "generating")) {
            task.setStage("阅读");
        } else {
            task.setStage("解析");
        }
        task.setProgress(progressResponse.getProgress());
        task.setTotalProgress(progressResponse.getTotalProgress());
        Integer totalProgress = progressResponse.getTotalProgress();
        String docId = task.getDocId();
        if (Objects.equals(totalProgress, 100)) {
            if (StrUtil.isNotBlank(docId)) {
                documentInfoService.update(
                        Wrappers.lambdaUpdate(DocumentInfo.class)
                                .eq(DocumentInfo::getId,
                                        Long.parseLong(task.getDocId()))
                                .eq(DocumentInfo::getStatus, DocumentStatusEnum.RUNNING)
                                .set(DocumentInfo::getStatus, DocumentStatusEnum.HANDLER_SUCCESS)

                );
            }
            task.setComplete(true);
        }
        saveTaskInfo(task);
        return task;
    }

    @Resource
    private LockService lockService;

    @Resource
    private ConversationMessageService messageService;


    @Override
    public DocumentInfo getResult(String taskId,
                                  ConversationMessage messageChat,
                                  Consumer<TextResponse> consumer, Consumer<TaskInfoVO> taskInfoConsumer) {
        try {
            DocumentInfo documentInfo = null;
            TaskInfoVO taskInfo = getTask(taskId);
            if (Objects.nonNull(taskInfoConsumer)) {
                taskInfoConsumer.accept(taskInfo);
            }
            result(taskInfo, consumer);
            String docId = taskInfo.getDocId();
            String documentId = taskInfo.getDocumentId();
            if (StrUtil.isNotBlank(docId)) {
                long id = Long.parseLong(docId);
                if (StrUtil.isNotBlank(documentId)) {
                    documentInfoService.update(
                            Wrappers.lambdaUpdate(DocumentInfo.class)
                                    .eq(DocumentInfo::getId, id)
                                    .set(DocumentInfo::getStatus, DocumentStatusEnum.SUCCESS)
                                    .set(DocumentInfo::getDocumentId, documentId)
                                    .set(DocumentInfo::getUpdateTime, LocalDateTime.now())
                    );
                }
                documentInfo = documentInfoService.getOne(
                        Wrappers.lambdaQuery(DocumentInfo.class)
                                .eq(DocumentInfo::getId, id)
                );
            }
            if (StrUtil.isNotBlank(documentId)
                    && StrUtil.isNotBlank(messageChat.getParentId())) {
                // 更新父的 message为基本信息
                messageService.update(
                        Wrappers.lambdaUpdate(ConversationMessage.class)
                                .eq(ConversationMessage::getConversationId, messageChat.getConversationId())
                                .eq(ConversationMessage::getMessageId, messageChat.getParentId())
                                .set(ConversationMessage::getContent, documentId)
                );
            }
            return documentInfo;

        } finally {
            deleteTaskInfo(taskId);
        }
    }

    /**
     * 消费消息
     *
     * @param taskInfo 任务id
     * @param consumer 消费信息
     */
    private void result(TaskInfoVO taskInfo, Consumer<TextResponse> consumer) {
        String taskId = taskInfo.getTaskId();
        if (!taskInfo.getComplete()) {
            String message = StrUtil.format("任务{}:{}未完成", taskId, taskInfo.getId());
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, message);
        }
        String language = taskInfo.getLanguage();
        String md5 = taskInfo.getMd5();
        // 获取基本信息
        if (taskId.startsWith(DOC_CACHE_TASK_ID_PREFIX)) {
            // 从数据库里面拿到
            DocumentSummary summary = documentSummaryService.getOne(
                    Wrappers.lambdaQuery(DocumentSummary.class)
                            .eq(DocumentSummary::getMd5, md5)
                            .eq(DocumentSummary::getLanguage, language)
            );
            if (Objects.isNull(summary)) {
                throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR,
                        StrUtil.format("{}:{}不存在", md5, language));
            }
            TextResponse textResponse = new TextResponse();
            MessageEnum status = MessageEnum.getMessage(summary.getStatus());
            textResponse.setStatus(Objects.isNull(status) ? MessageEnum.SUCCESS : status);
            textResponse.setDone(false);
            textResponse.setType(MessageConstants.SAVE_DOCUMENT_KEY);
            consumer.accept(textResponse);
            textResponse.setType(MessageConstants.SUMMARY_KEY);
            textResponse.setContent(summary.getSummary());
            textResponse.setDone(false);
            consumer.accept(textResponse);
            textResponse.setContent("");
            textResponse.setDone(true);
            consumer.accept(textResponse);
            String questions = summary.getQuestions();
            List<MessageChatVO.TextVO> text = JacksonUtils.readValueAsList(questions, MessageChatVO.TextVO.class);
            for (MessageChatVO.TextVO textVO : text) {
                textResponse.setType(MessageConstants.QUESTION_KEY);
                textResponse.setDone(false);
                textResponse.setContent(textVO.getText() + "\n");
                consumer.accept(textResponse);
            }
            textResponse.setDone(true);
            consumer.accept(textResponse);
        } else {
            //
            StringBuilder summary = new StringBuilder();
            StringBuilder questions = new StringBuilder();
            MessageEnum status = MessageEnum.SUCCESS;
            boolean error = false;
            Flowable<TextResponse> flowable = indexApiService.summary(taskInfo.getId());
            for (TextResponse textResponse : flowable.blockingIterable()) {
                String text = textResponse.getContent();
                switch (textResponse.getType()) {
                    case MessageConstants.SAVE_DOCUMENT_KEY:
                        if (StrUtil.isNotBlank(text)) {
                            Map map = JacksonUtils.readValue(text, Map.class);
                            Boolean limited = (Boolean) map.get(MessageConstants.DOCUMENT_TOKEN_LIMIT_KEY);
                            if (Objects.equals(true, limited)) {
                                status = MessageEnum.EXCEED_TOKEN;
                            }
                            textResponse.setStatus(status);
                        }
                        break;
                    case MessageConstants.ERROR_KEY:
                        error = true;
                        break;
                    case MessageConstants.QUESTION_KEY:
                        if (Objects.nonNull(text)) {
                            questions.append(text);
                        }
                        break;
                    case MessageConstants.SUMMARY_KEY:
                        if (Objects.nonNull(text)) {
                            summary.append(text);
                        }
                        break;
                }
                consumer.accept(textResponse);
            }
            log.info("summary:{},questions:{}", summary, questions);
            if (!error && StrUtil.isNotBlank(md5)) {
                try {
                    Lock lock = lockService.getLock(StrUtil.format("document:{}:{}", md5, language));
                    if (lock.tryLock()) {
                        try {
                            long count = documentSummaryService.count(
                                    Wrappers.lambdaQuery(DocumentSummary.class)
                                            .eq(DocumentSummary::getMd5, md5)
                                            .eq(DocumentSummary::getLanguage, language)
                            );
                            if (count <= 0) {
                                DocumentSummary documentSummary = new DocumentSummary();
                                documentSummary.setId(IdUtil.getSnowflakeNextId());
                                documentSummary.setLanguage(language);
                                documentSummary.setMd5(md5);
                                documentSummary.setStatus(status.getCode());
                                documentSummary.setCreateTime(LocalDateTime.now());
                                documentSummary.setSummary(summary.toString());
                                List<String> split = StrUtil.split(questions, "\n");
                                List<MessageChatVO.TextVO> text = Lists.newArrayList();
                                for (int i = 0; i < split.size(); i++) {
                                    MessageChatVO.TextVO textVO = new MessageChatVO.TextVO();
                                    textVO.setKey(String.valueOf(i + 1));
                                    textVO.setText(split.get(i));
                                    text.add(textVO);
                                }
                                documentSummary.setDocumentId(taskInfo.getDocumentId());
                                documentSummary.setQuestions(JacksonUtils.writeValueAsString(text));
                                documentSummaryService.save(documentSummary);
                            }
                        } finally {
                            lock.unlock();
                        }
                    }
                } catch (Exception e) {
                    log.error(e.getMessage(), e);
                    throw e;
                }
            }

        }
    }
