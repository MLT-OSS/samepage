    private static final String DOC_CACHE_TASK_ID_PREFIX = "doc:cache:";

    /**
     * 构建 task id
     *
     * @param text 内容信息
     * @return task id
     */
    private String getTaskId(String text, Long userId) {
        if (StrUtil.isNotBlank(text)) {
            return TEXT_PREFIX + userId + ":" + IdUtil.fastUUID();
        }
        return DOC_PREFIX + userId + ":" + IdUtil.fastUUID();
    }


