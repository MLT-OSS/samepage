    @Override
    public void submitTask(String botKey,
                           BotTypeEnum botType,
                           String model,
                           Long convId,
                           ConversationMessage response,
                           List<String> tools, List<ConversationMessage> request) throws LimiterException {
        ConversationInfo conversationInfo = conversationInfoMapper.getConversationInfo(
                botType.getConversationTable(),
                botKey,
                convId,
                null, null
        );
        if (conversationInfo == null) {
            throw new BusinessException(StrUtil.format("Conversation {} does not exist", convId));
        }

        String threadId = conversationInfo.getThreadId();
        Long id = conversationInfo.getId();
        Long createId = conversationInfo.getCreateId();
        // add lock
        Lock lock = lockService.getLock(StrUtil.format("assistant:lock:{}:{}", botType, id));
        lock.lock();
        try {
            String key = StrUtil.format("assistant:exec:{}:{}", botType, id);
            if (Boolean.TRUE.equals(redisTemplate.hasKey(key))) {
                throw new LimiterException("The assistant API can only support the execution of one task simultaneously with one thread.");
            }
            // 创建 thread
            if (StrUtil.isBlank(threadId)) {
                threadId = assistantService.createThread(Collections.emptyList(), Collections.emptyMap(), createId);
                conversationInfo.setThreadId(threadId);
                conversationBotService.updateConvThreadId(botType.getConversationTable(),
                        conversationInfo.getId(), threadId);
            }
            // 创建消息，如果是消息的model 不是assistantId(即和conversation info model 保持一致)则消息未创建
            if (Objects.nonNull(request)) {
                LocalDateTime now = LocalDateTime.now();
                for (ConversationMessage convMsg : request) {
                    if (!Objects.equals(convMsg.getRole(), ChatRoleEnum.user)
                            || StrUtil.isNotBlank(convMsg.getSource())
                    ) {
                        continue;
                    }
                    String content = MessageUtil.getContent(convMsg);
                    if (StrUtil.isNotBlank(content)) {
                        AssistantMessageRequest messageRequest = AssistantMessageRequest.builder().content(
                                content
                        ).role(ChatRoleEnum.user.getChatMessageRole().value()).build();
                        String message = assistantService.createMessage(
                                threadId, messageRequest, createId
                        );
                        convMsg.setSource(message);
                        convMsg.setUpdateTime(now);
                        conversationMessageMapper.updateMsg(
                                botType.getConversationMsgTable(), convMsg, convMsg.getContentType().getCode(), null
                        );
                    }
                }
            }
            // 创建runs
            String runsId = assistantService.createRuns(
                    botKey, threadId, createId
            );
            // 更新response 的状态
            AssistantSource source = new AssistantSource.AssistantSourceBuilder()
                    .assistantId(botKey).runsId(runsId).threadId(threadId).build();
            response.setSource(JacksonUtils.writeValueAsString(source));
            response.setUpdateTime(LocalDateTime.now());
            response.setStatus(MessageEnum.ASYNC_RUNNING);
            redisTemplate.opsForValue().set(key, runsId, 2, TimeUnit.HOURS);
        } finally {
            lock.unlock();
        }

    }

    @Data
    @Builder
    @NoArgsConstructor
    @AllArgsConstructor
    public static class AssistantSource {

        /**
         * Runs ID
         */
        private String runsId;

        /**
         * Thread id
         */
        private String threadId;

        /**
         * message id
         */
        private String messageId;


        /**
         * assistant id
         */
        private String assistantId;

    }

    @Override
    public ConversationMessage getTaskStatus(BotTypeEnum botType, String botKey, Long userId, Long convId, String msgId) {
        ConversationMessage msg = getMsg(
                botType,
                userId, convId, msgId);
        MessageEnum status = msg.getStatus();
        if (!Objects.equals(status, MessageEnum.ASYNC_RUNNING)
                && !Objects.equals(status, MessageEnum.COMPLETED)) {
            return msg;
        }
        String content = null;
        MessageEnum messageStatus = null;
        try {
            String source = msg.getSource();
            Long createId = msg.getCreateId();
            if (StrUtil.isBlank(source)) {
                throw new BusinessException("get assistant info failed");
            }
            AssistantSource assistantSource = JacksonUtils.readValue(source, AssistantSource.class);
            if (Objects.isNull(assistantSource)) {
                throw new BusinessException("get assistant info failed");
            }
            String threadId = assistantSource.getThreadId();
            String runsId = assistantSource.getRunsId();
            if (StrUtil.isNotBlank(threadId) && StrUtil.isNotBlank(runsId)) {
                Run run = assistantService.getRun(threadId, runsId, createId);
                if (Objects.isNull(run)) {
                    content = "assistant run not found.";
                    messageStatus = MessageEnum.SERVER_ERROR_CHINESE;
                } else {
                    switch (run.getStatus()) {
                        case "requires_action":
                            content = "assistant runs is requires_action.";
                            messageStatus = MessageEnum.SERVER_ERROR_CHINESE;
                            break;
                        case "in_progress":
                        case "completed":
                            List<RunStep> runSteps = assistantService.listRunsStatus(threadId, runsId, userId);
                            if (CollectionUtil.isEmpty(runSteps)) {
                                msg.setContent("assistant runs is in progress.");
                                return msg;
                            } else {
                                RunStep runStep = runSteps.get(0);
                                String type = runStep.getType();
                                if (Objects.equals(type, "message_creation")) {
                                    StepDetails stepDetails = runStep.getStepDetails();
                                    String messageId = null;
                                    if (Objects.nonNull(stepDetails)) {
                                        MessageCreation messageCreation = stepDetails.getMessageCreation();
                                        if (Objects.nonNull(messageCreation)) {
                                            messageId = messageCreation.getMessageId();
                                        }
                                    }
                                    assistantSource.setMessageId(messageId);
                                    source = JacksonUtils.writeValueAsString(assistantSource);
                                    // 获取消息的创建状态
                                    if (Objects.equals(runStep.getStatus(), "completed")) {
                                        // 获取 正文信息
                                        // 如果是 completed 已完成则已经完成
                                        if (StrUtil.isNotBlank(messageId)) {
                                            msg = getComplete(msg, assistantSource);
                                            content = getMessageContent(msg.getContentType(), assistantSource, userId);
                                            msg.setSource(source);
                                            if (StrUtil.isBlank(content)) {
                                                throw new BusinessException("The content replied by the assistant is empty");
                                            }
                                            redisTemplate.delete(StrUtil.format("assistant:exec:{}:{}", botType, convId));
                                            messageStatus = MessageEnum.SUCCESS;
                                        }
                                    } else {
                                        // 出现一个这个类型的run-step, 则可以调流式接口
                                        messageStatus = MessageEnum.COMPLETED;
                                        msg.setSource(source);
                                    }
                                } else {
                                    content = "assistant runs is in progress.";
                                    return msg;
                                }
                            }
                            break;
                        case "queued":
                            msg.setContent("assistant runs is queued.");
                            return msg;
                        case "cancelled":
                        case "cancelling":
                            content = "assistant runs is cancelled.";
                            messageStatus = MessageEnum.SERVER_ERROR_CHINESE;
                            break;
                        case "failed":
                            content = run.getLastError().getMessage();
                            messageStatus = MessageEnum.SERVER_ERROR_CHINESE;
                            break;
                        case "expired":
                            content = "assistant runs is expired.";
                            messageStatus = MessageEnum.SERVER_ERROR_CHINESE;
                            break;
                        default:
                            break;
                    }
                }
            } else {
                throw new BusinessException("Thread ID and run ID do not exist.");
            }
            if (StrUtil.isNotBlank(content)) {
                msg.setContent(content);
            }
            if (Objects.nonNull(messageStatus)) {
                msg.setStatus(messageStatus);
            }
            msg.setUpdateTime(LocalDateTime.now());
            conversationMessageMapper.updateMsg(
                    botType.getConversationMsgTable(), msg, msg.getContentType().getCode(), null
            );
            return msg;
        } catch (Exception e) {
            log.info(e.getMessage(), e);
            msg.setContent(content);
            msg.setStatus(MessageEnum.SERVER_ERROR_CHINESE);
            conversationMessageMapper.updateMsg(
                    botType.getConversationMsgTable(), msg, msg.getContentType().getCode(), null
            );
            return msg;
        }
    }


    private String getMessageContent(ContentTypeEnum contentType, AssistantSource source, Long userId) {
        if (Objects.isNull(source) || StrUtil.isBlank(source.messageId) || StrUtil.isBlank(source.threadId)) {
            throw new BusinessException(StrUtil.format("get assistant  message failed(params:{})", source));
        }
        AssistantMessage message = assistantService.retrieveMessage(source.threadId, source.messageId, userId);
        if (Objects.nonNull(message)) {
            List<MessageContent> messageContents = message.getContent();
            if (CollectionUtil.isNotEmpty(messageContents)) {
                List<MsgDTO> msgList = Lists.newArrayList();
                for (MessageContent messageContent : messageContents) {
                    switch (messageContent.getType()) {
                        case "text":
                            Text text = messageContent.getText();
                            if (Objects.nonNull(text)) {
                                String value = text.getValue();
                                if (Objects.nonNull(value)) {
                                    msgList.add(MsgDTO.text(value));
                                }
                            }
                            break;
                        case "image_file":
                            // todo 后续添加溯源文件
                            break;
                    }
                }
                switch (contentType) {
                    case reply_doc_source:
                        StringBuilder sb = new StringBuilder();
                        for (MsgDTO msgDTO : msgList) {
                            String type = msgDTO.getType();
                            if (Objects.equals(type, INPUT_TEXT_TYPE)) {
                                sb.append(msgDTO.textValue());
                            }
                        }
                        return sb.toString();
                    default:
                        return JacksonUtils.writeValueAsString(msgList);


                }

            }
        }
        throw new BusinessException(
                StrUtil.format("Failed to obtain the message information (thread:{},message:{}) for the assistant",
                        source.threadId, source.messageId));

    }

    /**
     * Retrieves a complete conversation message.
     *
     * @param msg             The message info.
     * @param assistantSource The assistant source info.
     * @return The conversation message.
     */
    private ConversationMessage getComplete(ConversationMessage msg,
                                            AssistantSource assistantSource) {

        // Statistics token
        // Building sources and network retrieval issues
        MessageEnum messageEnum = msg.getStatus();
        if (!Objects.equals(messageEnum, MessageEnum.ASYNC_RUNNING) && !Objects.equals(messageEnum, MessageEnum.COMPLETED)) {
            throw new BusinessException(StrUtil.format("messsage status is {}", msg.getStatus()));
        }
        Long createId = msg.getCreateId();
        List<SourceText> sourceTexts = Lists.newArrayList();
        List<RunStep> runsStatus = assistantService.listRunsStatus(assistantSource.threadId,
                assistantSource.runsId, createId);
        if (CollectionUtil.isNotEmpty(runsStatus)) {
            List<RunStep> completed = runsStatus.stream().filter(
                    status -> Objects.equals(status.getStatus(), "completed")
                            && Objects.equals(status.getType(), "tool_calls")
            ).collect(Collectors.toList());
            if (CollectionUtil.isNotEmpty(completed)) {
                for (RunStep runStep : completed) {
                    StepDetails stepDetails = runStep.getStepDetails();
                    if (Objects.nonNull(stepDetails)) {
                        if (Objects.equals(stepDetails.getType(), "tool_calls")) {
                            List<ToolCall> toolCalls = stepDetails.getToolCalls();
                            if (CollectionUtil.isNotEmpty(toolCalls)) {
                                for (ToolCall toolCall : toolCalls) {
                                    ToolCallFunction function = toolCall.getFunction();
                                    log.info("获取toolcall 信息：{}", function);
                                    if (Objects.nonNull(function)) {
                                        switch (function.getName()) {
                                            case "web_search":
                                                // Ask questions
                                                String arguments = function.getArguments();
                                                if (StrUtil.isNotBlank(arguments)) {
                                                    Map query = JacksonUtils.readValue(arguments, Map.class);
                                                    Object question = query.get("query");
                                                    if (Objects.nonNull(question)) {
                                                        msg.setQuestion(String.valueOf(question));
                                                    }
                                                }
                                                // build web url source
                                                String output = function.getOutput();
                                                if (StrUtil.isNotBlank(output)) {
                                                    List<SourceText> source = JacksonUtils.readValueAsList(output, SourceText.class);
                                                    if (CollectionUtil.isNotEmpty(source)) {
                                                        int size = source.size();
                                                        for (int i = 0; i < size; i++) {
                                                            SourceText sourceText = source.get(i);
                                                            if (StrUtil.isNotBlank(sourceText.text)
                                                                    || StrUtil.isNotBlank(sourceText.title)
                                                                    || StrUtil.isNotBlank(sourceText.text)
                                                                    || StrUtil.isNotBlank(sourceText.link)
                                                            ) {
                                                                sourceTexts.add(sourceText);
                                                            }
                                                            sourceText.setKey(String.valueOf(i));
                                                            sourceText.setType(ContentTypeEnum.web_search.name());
                                                        }

                                                    }
                                                }
                                                break;
                                            case "retrieval":
                                                output = function.getOutput();
                                                if (StrUtil.isNotBlank(output)) {
                                                    Map map = JacksonUtils.readValue(output, Map.class);
                                                    if (CollectionUtil.isNotEmpty(map)) {
                                                        AtomicInteger index = new AtomicInteger(sourceTexts.size());
                                                        map.forEach((k, v) -> {
                                                                    if (Objects.isNull(v)) {
                                                                        return;
                                                                    }
                                                                    String value = String.valueOf(v);
                                                                    if (StrUtil.isBlank(value)) {
                                                                        return;
                                                                    }
                                                                    sourceTexts.add(new SourceText.SourceTextBuilder()
                                                                            .key(String.valueOf(index.incrementAndGet()))
                                                                            .type(ContentTypeEnum.reply_doc_source.name())
                                                                            .text(value)
                                                                            .title(String.valueOf(k)).build());
                                                                }

                                                        );

                                                    }
                                                }
                                                break;
                                            default:
                                                // todo function render
                                                break;
                                        }
                                    }
                                }
                            }

                        }

                    }
                }
            }
            // update content type and question
            if (StrUtil.isNotBlank(msg.getQuestion())) {
                msg.setContentType(ContentTypeEnum.web_search);
            } else if (CollectionUtil.isNotEmpty(sourceTexts)) {
                msg.setContentType(ContentTypeEnum.reply_doc_source);
            } else {
                msg.setContentType(ContentTypeEnum.reply_input);
            }
            if (CollectionUtil.isNotEmpty(sourceTexts)) {
                msg.setReference(JacksonUtils.writeValueAsString(sourceTexts));
            }
        }
        msg.setStatus(MessageEnum.COMPLETED);
        return msg;

    }


