    @Resource
    private BotMessageService botMessageService;


    @Override
    public void streamTaskResult(
            BotInfo botInfo,
            Long userId,
            Long convId,
            String msgId,
            Consumer<ConversationInfoServiceImpl.Message> consumer,
            String sseId) {
        ConversationMessage chatMsg = null;
        ConversationInfoServiceImpl.Message message = null;
        BotTypeEnum botType = botInfo.getType();
        // lock
        Lock lock = lockService.getLock(StrUtil.format("assistant:completed:{}:{}:{}",
                botType, convId, msgId));
        lock.lock();
        try {
            chatMsg = getMsg(
                    botType,
                    userId,
                    convId,
                    msgId
            );
            if (Objects.isNull(chatMsg)) {
                throw new BusinessException("message not found");
            }
            MessageEnum status = chatMsg.getStatus();
            if (!Objects.equals(status, MessageEnum.COMPLETED)) {
                throw new BusinessException("task not completed");
            }
            String source = chatMsg.getSource();
            if (StrUtil.isBlank(source)) {
                throw new BusinessException(StrUtil.format("message {} not found assistant source", msgId));
            }
            AssistantSource assistantSource = JacksonUtils.readValue(source, AssistantSource.class);
            if (Objects.isNull(assistantSource)) {
                throw new BusinessException(StrUtil.format("message {} not found assistant source", msgId));
            }
            chatMsg = getComplete(chatMsg, assistantSource);
            message = copy(chatMsg, sseId);
            message.setStatus(MessageEnum.SUCCESS.getType());
            String question = chatMsg.getQuestion();
            message.setContentType(chatMsg.getContentType());
            if (StrUtil.isNotBlank(question)) {
                message.setQuestion(question);
                consumer.accept(message);
            }
            String reference = chatMsg.getReference();
            if (StrUtil.isNotBlank(reference)) {
                List<MessageChatVO.TextVO> text =
                        JacksonUtils.readValueAsList(reference, MessageChatVO.TextVO.class);
                if (CollectionUtil.isNotEmpty(text)) {
                    for (MessageChatVO.TextVO t : text) {
                        message.setReference(t);
                        consumer.accept(message);
                    }
                }
            }
            StringBuilder content = new StringBuilder();
            ContentTypeEnum contentType = chatMsg.getContentType();
            Flowable<CompletionChunk> chatCompletionChunk = assistantService.messageStream(assistantSource.threadId, assistantSource.runsId, userId);
            for (CompletionChunk completionChunk : chatCompletionChunk.blockingIterable()) {
                List<CompletionChunk.CompletionChoice> choices = completionChunk.getChoices();
                if (CollectionUtil.isNotEmpty(choices)) {
                    CompletionChunk.CompletionChoice chatCompletionChoice = choices.get(0);
                    CompletionChunk.ChatMessage msg = chatCompletionChoice.getMessage();
                    if (Objects.nonNull(msg)) {
                        String text = msg.getContent();
                        if (Objects.nonNull(text)) {
                            switch (contentType) {
                                case reply_doc_source:
                                    message.setContent(text);
                                    content.append(text);
                                    consumer.accept(message);
                                    break;
                                case reply_input:
                                case web_search:
                                default:
                                    message.text(text);
                                    content.append(text);
                                    consumer.accept(message);
                                    break;
                            }

                        }
                    }
                }
            }
            switch (contentType) {
                case reply_doc_source:
                    chatMsg.setContent(content.toString());
                    break;
                default:
                    chatMsg.setContent(JacksonUtils.writeValueAsString(Collections.singletonList(MsgDTO.text(content.toString()))));
                    break;
            }
            chatMsg.setStatus(MessageEnum.SUCCESS);
        } catch (Exception e) {
            log.error(e.getMessage(), e);
            if (Objects.nonNull(message)) {
                message.setStatus(MessageEnum.SERVER_ERROR_CHINESE.getType());
                message.setContent(MessageEnum.SERVER_ERROR_CHINESE.getDesc());
            }
            consumer.accept(message);
            if (Objects.nonNull(chatMsg)) {
                chatMsg.setContent(e.getMessage());
                chatMsg.setStatus(MessageEnum.SERVER_ERROR_CHINESE);
            }
        } finally {
            botMessageService.updateMsg(
                    botInfo, chatMsg, null, sseId
            );
            lock.unlock();
            redisTemplate.delete(StrUtil.format("assistant:exec:{}:{}", botType, convId));
            consumer.accept(null);
        }


    }

    /**
     * copy message
     *
     * @param msg        会话消息
     * @param sseEmitter
     * @return
     */
    private ConversationInfoServiceImpl.Message copy(ConversationMessage msg, String sseEmitter) {
        ConversationInfoServiceImpl.Message message = new ConversationInfoServiceImpl.Message();
        message.setStatus(MessageConstants.MESSAGE_SUCCESS_STATUS);
        message.setMessageId(msg.getMessageId());
        message.setType(MessageTypeEnum.text);
        message.setConversationId(msg.getConversationId().toString());
        message.setCreateTime(msg.getCreateTime());
        message.setCreateId(msg.getCreateId());
        message.setRole(msg.getRole());
        message.setContentType(msg.getContentType());
        message.setContent(null);
        message.setParentMessageId(msg.getParentId());
        message.setLike(MessageLikeEnum.unknown);
        message.setConnectId(sseEmitter);
        return message;
    }


    /**
     * Retrieves a message based on the specified bot type, user ID, conversation ID, and message ID.
     *
     * @param botType The bot type.
     * @param userId  The user's ID.
     * @param convId  The conversation ID.
     * @param msgId   The message ID.
     * @return The corresponding message.
     */
    private ConversationMessage getMsg(BotTypeEnum botType, Long userId, Long convId, String msgId) {
        return conversationMessageMapper.getMsgById(
                botType.getConversationTable(),
                botType.getConversationMsgTable(),
                userId,
                convId,
                msgId
        );
    }


    @Override
    public void cancelTask(BotTypeEnum botType,
                           String botKey,
                           Long userId,
                           Long convId,
                           String msgId) {
        try {
            ConversationMessage msg = getMsg(botType, userId, convId, msgId);
            if (Objects.nonNull(msg)) {
                String source = msg.getSource();
                if (StrUtil.isNotBlank(source)) {
                    AssistantSource assistantSource = JacksonUtils.readValue(source, AssistantSource.class);
                    assistantService.cancelRun(assistantSource.getThreadId(), assistantSource.getRunsId(), userId);
                }
            }
        } catch (Exception ex) {
            log.error(ex.getMessage(), ex);
        } finally {
            redisTemplate.delete(StrUtil.format("assistant:exec:{}:{}", botType, convId));
        }

    }
