        BotModelDTO botModel = JacksonUtils.readValue(botInfo.getModelConfig(), BotModelDTO.class);
        // 构建template 参数
        String templateConfig = botInfo.getTemplateConfig();
        List<TemplateConfigDto> params = null;
        if (StrUtil.isNotBlank(templateConfig)) {
            params = JacksonUtils.readValueAsList(templateConfig, TemplateConfigDto.class);
        }
        ConversationMessage convMsg = createConvMsg(botInfo, userId, command, botModel, params);
        // 创建sse
