    /**
     * 保存会话信息
     *
     * @param bot        基本信息
     * @param msgCommand 参数信息
     * @param userId     用户id
     * @return 会话消息
     */
    private ConversationInfo save(BotInfo bot, ConversationMsgCommand msgCommand, Long userId,
                                  List<TemplateConfigDto> params) {
        List<ConversationMsgCommand.MessageInfoCommand> messages = msgCommand.getMessages();
        if (messages.isEmpty()) {
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "会话消息不能为空");
        }
        ConversationInfo info = new ConversationInfo();
        info.setCreateId(userId);
        info.setId(IdUtil.getSnowflakeNextId());
        info.setModel(bot.getKey());
        info.setIsDeleted(false);
        info.setWebUrl(msgCommand.getWebUrl());
        LocalDateTime now = LocalDateTime.now();
        info.setCreateTime(now);
        info.setUpdateTime(now);
        info.setLastMessageTime(now);
        ConversationTypeEnum convType = null;
        String title = null;
        ConversationMsgCommand.MessageInfoCommand msg = messages.get(messages.size() - 1);
        String doc = msg.getDocId();
        if (StrUtil.isNotBlank(doc)) {
            long docId = Long.parseLong(doc);
            info.setDocId(docId);
            // 获取文件名
            DocumentInfo docInfo = documentInfoService.getInfo(docId, userId);
            title = docInfo.getDocName();
        }
        String taskId = msg.getTaskId();
        if (StrUtil.isNotBlank(taskId)) {
            ContentTypeEnum contentType = msg.getContentType();
            TaskInfoVO taskInfo = taskService.getTaskInfo(taskId);
            switch (contentType) {
                case doc_link:
                    convType = ConversationTypeEnum.DOC;
                    String docId = taskInfo.getDocId();
                    if (StrUtil.isBlank(docId)) {
                        throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "获取不到任务的文档id");
                    }
                    info.setDocId(Long.parseLong(docId));
                    title = taskInfo.getName();
                    break;
                case read_article:
                    convType = ConversationTypeEnum.CHAT_INDEX;
                    title = msgCommand.getTitle();
                    break;
                default:
                    throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, StrUtil.format("消息的contentType:{}不支持", contentType));
            }
        } else {
            switch (msg.getContentType()) {
                case input:
                case reply_input:
                    title = MessageUtil.getContent(JacksonUtils.readValueAsList(msg.getContent(), MsgDTO.class), params);
                    break;
                default:
                    break;
            }
        }
        if (Objects.isNull(convType)) {
            convType = ConversationTypeEnum.COMMON;
        }
        if (StrUtil.isBlank(title)) {
            if (Objects.equals(convType, ConversationTypeEnum.DOC)) {
                title = "未知文件标题";
            } else {
                title = "未知标题";
            }
        }
        title = title.substring(0, Math.min(Constants.CONV_TITLE_LENGTH, title.length()));
        info.setTitle(title);
        info.setType(convType);
        convBotService.save(bot, info);
        return info;
    }


