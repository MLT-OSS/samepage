    /**
     * 保存消息信息
     *
     * @param botInfo  机器人
     * @param convInfo 会话
     * @param messages 消息列表
     * @param userId   用户id
     * @return 是否是保存
     */
    private boolean saveMsg(BotInfo botInfo, Long userId, ConversationInfo convInfo,
                            List<ConversationMsgCommand.MessageInfoCommand> messages) {
        if (messages.isEmpty()) {
            return false;
        }
        Long id = convInfo.getId();
        List<String> msgIds = messages.stream().map(ConversationMsgCommand.MessageInfoCommand::getMessageId).collect(Collectors.toList());
        List<ConversationMessage> msg = convMsgBotService.listMessage(botInfo, userId, id, msgIds.toArray(new String[0]));
        for (ConversationMessage conversationMessage : msg) {
            String messageId = conversationMessage.getMessageId();
            msgIds.remove(messageId);

        }
        List<ConversationMessage> batch = Lists.newArrayList();
        for (ConversationMsgCommand.MessageInfoCommand message : messages) {
            String messageId = message.getMessageId();
            if (msgIds.contains(messageId)) {
                // 开始创建信息
                LocalDateTime now = LocalDateTime.now();
                ConversationMessage convMsg = conversationConvert.toMessage(message);
                convMsg.setModel(botInfo.getKey());
                convMsg.setId(IdUtil.getSnowflakeNextId());
                convMsg.setLike(MessageLikeEnum.unknown.name());
                convMsg.setConversationId(id);
                convMsg.setIsDeleted(false);
                convMsg.setCreateId(userId);
                convMsg.setCreateTime(now);
                convMsg.setUpdateTime(now);
                batch.add(convMsg);
            }
        }
        if (!batch.isEmpty()) {
            convMsgBotService.saveBatch(botInfo, batch);
        }
        return !batch.isEmpty();
    }

