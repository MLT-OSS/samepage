    @Override
    public Pair<SseEmitter, ConversationMessage> handler(BotInfo botInfo, Long userId,
                                                         ConversationMsgCommand msgCommand, String locale) {
        if (Objects.equals(ChatModelEnum.chat_lite.getName(), botInfo.getKey())
                || Objects.equals(ChatModelEnum.chat_pro.getName(), botInfo.getKey())) {
            msgCommand.setModel(botInfo.getKey());
            return new Pair<>(conversationInfoService.message(msgCommand, userId, locale), null);
        }
        long start = System.currentTimeMillis();
        String modelConfig = botInfo.getModelConfig();
        BotModelDTO botModel = null;
        if (StrUtil.isNotBlank(modelConfig)) {
            botModel = JacksonUtils.readValue(modelConfig, BotModelDTO.class);
        }

        // 1. upsert conversationInfo
        String conversationId = msgCommand.getConversationId();
        List<ConversationMsgCommand.MessageInfoCommand> messages = msgCommand.getMessages();
        if (messages.isEmpty()) {
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "会话消息体不能为空");
        }
        Long convId;
        ConversationInfo convInfo;
        if (StrUtil.isNotBlank(conversationId)) {
            convId = Long.parseLong(conversationId);
            // 获取conversation
            convInfo = convBotService.getConvInfo(botInfo, userId, convId);
        } else {
            convInfo = save(botInfo, msgCommand, userId, null);
            convId = convInfo.getId();
        }
        String msgId = IdUtil.fastUUID();
        String sseEmitter = null;
        if (!Objects.equals(botInfo.getIsStream(), false)) {
            sseEmitter = msgSseEmitterService.createSseEmitter(userId, convId, msgId);
        }
        // 2. upset messages
        if (!messages.isEmpty()) {
            saveMsg(botInfo, userId, convInfo, msgCommand.getMessages());
        }
        ConversationMsgCommand.MessageInfoCommand command = messages.get(messages.size() - 1);
        ContentTypeEnum contentType = ContentTypeEnum.text;
        ContentTypeEnum typeEnum = command.getContentType();
        switch (typeEnum) {
            case read_article:
            case read_doc:
                contentType = ContentTypeEnum.reply_summary;
                break;
            case input:
                contentType = ContentTypeEnum.reply_input;
                break;
            default:
                switch (convInfo.getType()) {
                    case CHAT_INDEX:
                        contentType = ContentTypeEnum.reply_source;
                        break;
                    case DOC:
                        contentType = ContentTypeEnum.reply_doc_source;
                        break;
                    default:
                        break;
                }
        }
        // 3， 创建回答的信息
        ConversationMessage convMsg = new ConversationMessage();
        convMsg.setMessageId(msgId);
        convMsg.setId(IdUtil.getSnowflakeNextId());
        convMsg.setParentId(command.getMessageId());
        convMsg.setContent(MessageConstants.MESSAGE_RUNNING);
        convMsg.setType(MessageTypeEnum.text);
        convMsg.setLike(MessageLikeEnum.unknown.name());
        convMsg.setModel(botInfo.getKey());
        convMsg.setRole(ChatRoleEnum.assistant);
        convMsg.setCreateId(userId);
        convMsg.setStatus(MessageEnum.RUNNING);
        convMsg.setIsDeleted(false);
        convMsg.setCreateTime(LocalDateTime.now());
        convMsg.setUpdateTime(convMsg.getCreateTime());
        convMsg.setConversationId(convInfo.getId());
        convMsg.setContentType(contentType);
        convMsgBotService.saveBatch(botInfo, Collections.singletonList(convMsg));
        ConversationInfoServiceImpl.Message message = copy(convMsg, sseEmitter);
        send(message, sseEmitter);
        if (log.isInfoEnabled()) {
            log.info("sse-pre-cost-time: convId:{},msgId:{},cost:{}ms", convInfo.getId(),
                    convMsg.getMessageId(), System.currentTimeMillis() - start);
        }
        async(botInfo, botModel, null, msgCommand.getLanguage(), locale, message, convMsg, sseEmitter);
        return new Pair<>(msgSseEmitterService.getSseEmitter(sseEmitter), convMsg);
    }

    private void async(BotInfo bot,
                       BotModelDTO botModel,
                       List<TemplateConfigDto> params,
                       String lang,
                       String locale,
                       ConversationInfoServiceImpl.Message message,
                       ConversationMessage convMsg,
                       String sseEmitter) {
        try {
            String templatePrompt = bot.getTemplatePrompt();
            PromptConfigDTO prompt;
            if (StrUtil.isNotBlank(templatePrompt)) {
                prompt = JacksonUtils.readValue(templatePrompt, PromptConfigDTO.class);
            } else {
                prompt = null;
            }
            msgTaskExecutor.execute(() -> chat(bot,
                    botModel, params, prompt,
                    lang,
                    locale, message, convMsg, sseEmitter));
        } catch (RejectedExecutionException rejected) {
            log.error(rejected.getMessage(), rejected);
            MessageEnum exceed = MessageEnum.getMessage(MessageConstants.EXCEED_MESSAGE_TYPE, lang);
            message.setContent(exceed.getDesc());
            message.setStatus(MessageConstants.MESSAGE_FAILED_STATUS);
            convMsg.setContent(exceed.getDesc());
            convMsg.setStatus(exceed);
            send(message, sseEmitter);
            updateMsg(bot, convMsg, null, sseEmitter);
            close(sseEmitter);
        }
    }

