    private List<TemplateConfigDto> checkParam(BotInfo botInfo, List<TemplateConfigDto> params) {
        String config = botInfo.getTemplateConfig();
        if (StrUtil.isBlank(config)) {
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "模型的参数配置不能为空");
        }
        List<TemplateConfigDto> configDto = JacksonUtils.readValueAsList(config, TemplateConfigDto.class);
        Map<String, TemplateConfigDto> cache = Maps.newHashMap();
        for (TemplateConfigDto templateConfigDto : params) {
            cache.put(templateConfigDto.getKey(), templateConfigDto);
        }
        for (TemplateConfigDto param : configDto) {
            String key = param.getKey();
            TemplateConfigDto templateConfigDto = cache.get(key);
            String value = null;
            if (Objects.nonNull(templateConfigDto)) {
                value = templateConfigDto.getValue();
            }
            if (Objects.equals(param.getIsRequired(), true) && StrUtil.isBlank(value)) {
                throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "必填参数没有值");
            }
            param.setValue(value);
        }
        return configDto;
    }


    @Resource
    private RedisTemplate<String, String> redisTemplate;

    public void updateMsg(BotInfo botInfo,
                           ConversationMessage convMsg, String desc, String sId) {
        String cacheKey;
        if (StrUtil.isBlank(sId)) {
            cacheKey = MessageConstants.buildStopMsgKey(convMsg.getCreateId(), convMsg.getConversationId(), convMsg.getMessageId());
        } else {
            cacheKey = MessageConstants.buildStopMsgKey(sId);
        }
        String value = redisTemplate.opsForValue().get(cacheKey);
        if (StrUtil.isNotBlank(value)) {
            //redisTemplate.delete(cacheKey);
            return;
        }
        convMsgBotService.update(botInfo,
                convMsg);
        if (StrUtil.isBlank(value)) {
            MessageEnum status = convMsg.getStatus();
            if (!Objects.equals(status, MessageEnum.SUCCESS)) {
                desc = null;
            } else {
                desc = MessageUtil.getContent(convMsg);
            }
            // 更新 最新的修改时间和描述信息
            convBotService.updateDesc(botInfo, convMsg.getConversationId(), convMsg.getCreateId(), desc);
        }

    }

    private void close(String sseEmitter) {
        msgSseEmitterService.close(sseEmitter);
    }

    @Resource
    private BotHandler botHandler;


    private static final List<ContentTypeEnum> EXCLUDE_HISTORY = Arrays.asList(
            ContentTypeEnum.welcome, ContentTypeEnum.doc_link, ContentTypeEnum.virtual_root
    );

    @Resource
    private Map<String, ConvMsgTaskService> convMsgTaskService;


    private void chat(BotInfo bot,
                      BotModelDTO model,
                      List<TemplateConfigDto> params,
                      PromptConfigDTO prompt, String language, String locale,
                      ConversationInfoServiceImpl.Message message,
                      ConversationMessage convMsg,
                      String sseEmitter) {
        String desc = null;
        try {
            BotTypeEnum type = bot.getType();
            Consumer<ConversationInfoServiceImpl.Message> consumer = null;
            List<ConversationMessage> msg;
            // 获取input 的参数信息
            switch (type) {
                case assistant_chat:
                    ConvMsgTaskService taskService = convMsgTaskService.get(type.name());
                    if (Objects.isNull(taskService)) {
                        throw new BusinessException("获取不到task 执行器");
                    }
                    msg = convMsgBotService.listMsg(bot, convMsg.getConversationId(),
                            convMsg.getCreateId());
                    if (msg.isEmpty()) {
                        throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "用户问的问题未保存成功");
                    }
                    taskService.submitTask(bot.getKey(), type, null,
                            convMsg.getConversationId(), convMsg, null, msg);
                    return;
                case chat:
                    if (Objects.isNull(model)) {
                        throw new BusinessException("模型配置不能为空");
                    }
                    consumer = mess -> send(mess, sseEmitter);
                    String parentId = convMsg.getParentId();
                    msg = convMsgBotService.listMsg(bot, convMsg.getConversationId(),
                            convMsg.getCreateId());
                    if (msg.isEmpty()) {
                        throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "用户问的问题未保存成功");
                    }
                    // 获取当前的链路
                    Map<String, ConversationMessage> cache = Maps.newHashMap();
                    for (ConversationMessage conversationMessage : msg) {
                        cache.put(conversationMessage.getMessageId(), conversationMessage);
                    }
                    msg = Lists.newArrayList();
                    while (StrUtil.isNotBlank(parentId)) {
                        ConversationMessage conversationMessage = cache.get(parentId);
                        if (Objects.isNull(conversationMessage)
                                || EXCLUDE_HISTORY.contains(conversationMessage.getContentType())
                        ) {
                            break;
                        }
                        // 排除审核不通过的或者失败的，消息
                        MessageEnum status = conversationMessage.getStatus();
                        if (Objects.equals(status, MessageEnum.SUCCESS) || Objects.equals(status, MessageEnum.EXCEED_TOKEN)) {
                            msg.add(conversationMessage);
                        }
                        if (Objects.equals(parentId, conversationMessage.getParentId())) {
                            break;
                        }
                        parentId = conversationMessage.getParentId();
                    }
                    if (msg.size() > 1) {
                        msg = Lists.reverse(msg);
                    }
                    break;
                case stateless_chat:
                    if (Objects.isNull(model)) {
                        throw new BusinessException("模型配置不能为空");
                    }
                    msg = convMsgBotService.listMessage(bot, convMsg.getCreateId(), convMsg.getConversationId(),
                            convMsg.getParentId());
                    break;
                default:
                    throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, StrUtil.format("不支持会话类型"));
            }
            if (msg.isEmpty()) {
                throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "用户问的问题未保存成功");
            }

            List<BotLangDTO> botLang = Collections.emptyList();
            String langStr = bot.getLang();
            if (StrUtil.isNotBlank(langStr)) {
                botLang = JacksonUtils.readValueAsList(langStr, BotLangDTO.class);
            }
            String contentType = model.getContentType();
            ContentTypeEnum contentTypeEnum = ContentTypeEnum.reply_input;
            if (StrUtil.isNotBlank(contentType)) {
                contentTypeEnum = ContentTypeEnum.valueOf(contentType);
            }
            convMsg.setContentType(contentTypeEnum);
            message.setContentType(contentTypeEnum);
            if (CollectionUtil.isEmpty(msg)) {
                throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "所问的问题不能为空");
            }
            ModelConfig.ModelConfigBuilder builder = ModelConfig.builder()
                    .request(msg)
                    .msg(message)
                    .response(convMsg)
                    .locale(SessionUtil.getLocale(locale))
                    .botLang(botLang)
                    .params(params)
                    .consumer(consumer)
                    .lang(language);
            // 调用大模型信息
            desc = botHandler.handler(model, builder.build(), prompt, bot.getIsStream());
            MessageEnum status = convMsg.getStatus();
            if (Objects.isNull(status) || Objects.equals(status, MessageEnum.RUNNING)) {
                convMsg.setStatus(MessageEnum.SUCCESS);
            }
        } catch (Throwable e) {
            log.error(e.getMessage(), e);
            MessageEnum messageEnum = MessageEnum.getMessage(MessageConstants.MESSAGE_FAILED_STATUS, language);
            if (e instanceof LimiterException){
                messageEnum = MessageEnum.LIMITED_ERROR_CHINESE;
            }
            message.setContent(messageEnum.getDesc());
            message.setStatus(messageEnum.getType());
            convMsg.setContent(e.getMessage());
            convMsg.setStatus(messageEnum);
            send(message, sseEmitter);
        } finally {
            updateMsg(bot, convMsg, desc, sseEmitter);
            close(sseEmitter);
        }

    }

    /**
     * copy message
     *
     * @param msg        会话消息
     * @param sseEmitter
     * @return
     */
    private ConversationInfoServiceImpl.Message copy(ConversationMessage msg, String sseEmitter) {
        ConversationInfoServiceImpl.Message message = new ConversationInfoServiceImpl.Message();
        message.setStatus(MessageConstants.MESSAGE_SUCCESS_STATUS);
        message.setMessageId(msg.getMessageId());
        message.setType(MessageTypeEnum.text);
        message.setConversationId(msg.getConversationId().toString());
        message.setCreateTime(msg.getCreateTime());
        message.setCreateId(msg.getCreateId());
        message.setRole(msg.getRole());
        message.setContentType(msg.getContentType());
        message.setContent(null);
        message.setParentMessageId(msg.getParentId());
        message.setLike(MessageLikeEnum.unknown);
        message.setConnectId(sseEmitter);
        return message;
    }

    /**
     * 发送消息信息
     *
     * @param msg        消息列表
     * @param sseEmitter
     */
    private void send(ConversationInfoServiceImpl.Message msg, String sseEmitter) {
        try {
            msg.setSendTime(LocalDateTime.now().format(DateTimeFormatter.ISO_DATE_TIME));
            msgSseEmitterService.send(sseEmitter, conversationConvert.copyMessage(msg));
            msg.setContent(null);
            msg.setSuggest(null);
            msg.setReference(null);
            msg.setFinished(null);
            msg.setTitle(null);
            msg.setInput(null);
            msg.setQuestion(null);
        } catch (Throwable throwable) {
            log.error(throwable.getMessage(), throwable);
        }

    }


