@RequiredArgsConstructor
@Slf4j
@Service
public class LoginServiceImpl implements ILoginService {


    @Resource
    private IUserService userService;
    @Resource
    private StringRedisTemplate stringRedisTemplate;

    private final CheckCodeService checkCodeService;


    @Resource
    private RedisTemplate<String, UserContext> redisTemplate;


    @Resource
    private SystemProperties systemProperties;


    private void checkMail(String mail) {
        String mailRegex = systemProperties.getMailRegex();
        if (StrUtil.isNotBlank(mailRegex)) {
            if (!Pattern.matches(mailRegex, mail)) {
                throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "不支持该类型邮箱");
            }
        }
    }

    @Override
    public UserRegisterEnum checkIfPhoneNumberExists(String phoneNumber) {
        checkMail(phoneNumber);
        User user = userService.get(phoneNumber);
        if (Objects.isNull(user)) {
            return UserRegisterEnum.UNREGISTERED;
        } else {
            return UserRegisterEnum.PERSONAL_REGISTERED;
        }
    }


    @Override
    public void logInWithPassword(String account, String password, HttpServletRequest request) {
        // 密码错误次数
        int errorTimes = 0;
        String redisKey = "login_error_limit:" + account;
        // 手机号是否锁定登录
        if (Boolean.TRUE.equals(stringRedisTemplate.hasKey(redisKey))) {
            Object value = stringRedisTemplate.opsForValue().get(redisKey);
            if (Objects.isNull(value)) {
                errorTimes = 0;
            } else {
                errorTimes = Integer.parseInt(String.valueOf(value));
            }
        }
        // 检索用户
        User user = userService.get(account);
        if (null == user) {
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "该账号还未注册");
        }
        // 如果密码为空
        if (StrUtil.isEmpty(user.getPassword())) {
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "该账号还未设置密码，请尝试通过其他方式登录");
        }
        // 验证密码
        boolean isMatch = BCrypt.checkpw(password, user.getPassword());
        if (!isMatch) {
            // 累计失败次数
            stringRedisTemplate.opsForValue().increment(redisKey, 1);
            stringRedisTemplate.expire(redisKey, 5, TimeUnit.MINUTES);
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR,
                    errorTimes + 1 >= 3 ? "密码不正确，您可尝试找回密码或其他方式登录" : "密码不正确，请您重新输入");
        }

        String userKey = StrUtil.format(RedisKeyConst.USER_INFO, user.getId());
        redisTemplate.opsForValue().set(userKey, getUserContent(user.getId()),
                7, TimeUnit.DAYS);
        SessionUtil.saveUserInfo(request.getSession(), user, activeInterval);
    }


    @Value("${login.session.maxInterval: 3600}")
    private Integer activeInterval;


    @Transactional(rollbackFor = Exception.class)
    @Override
    public boolean resetPassword(PasswordQuery request) {
        checkCodeService.smsCode(request.getAccount(), request.getCode(), request.getSmsType());
        User user = userService.get(request.getAccount());
        if (null == user) {
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "该账号还未注册");
        }
        // hash密码
        String pwHash = BCrypt.hashpw(request.getPassword(), BCrypt.gensalt());
        User updateUser = new User();
        updateUser.setId(user.getId());
        // 重设密码
        updateUser.setPassword(pwHash);
        return userService.updateById(updateUser);
    }

    @Resource
    private RedisTemplate<String, User> userRedisTemplate;

    private static final String CACHE_USER_FORMAT = "user:pre:info:{}";

    @Transactional(rollbackFor = Exception.class)
    @Override
    public String signup(PasswordQuery request) {
        String mobile = request.getAccount();
        checkMail(mobile);
        //String inviteCode = request.getRegistrationInviteCode();
        Lock lock = lockService.getLock(StrUtil.format(REGISTER_LOCK_KEY, mobile));
        lock.lock();
        try {
            User user = userService.get(mobile);
            if (null != user) {
                throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "该手机号已注册");
            }
            checkCodeService.smsCode(mobile, request.getCode(), 1);
            user = createNewUser(mobile, request.getUsername(), StringPool.EMPTY);
            String pwHash = BCrypt.hashpw(request.getPassword(), BCrypt.gensalt());
            // 更新密码
            user.setPassword(pwHash);
            // 记录邀请码
            //user.setInviteCode(inviteCode);
            if (request.getSignup()) {
                userService.save(user);
            } else {
                // 放入redis 缓存
                userRedisTemplate.opsForValue().set(
                        StrUtil.format(CACHE_USER_FORMAT, user.getId()), user,
                        24, TimeUnit.HOURS
                );
            }
            return user.getId().toString();
        } finally {
            lock.unlock();
        }

    }


    @Resource
    private LockService lockService;


    private static final String REGISTER_LOCK_KEY = "user:register:{}";


    @Override
    public UserContext refreshUserContent(Long userId) {
        ValueOperations<String, UserContext> operations = redisTemplate.opsForValue();
        String userKey = StrUtil.format(RedisKeyConst.USER_INFO, userId);

        UserContext user = userService.getUser(userId);
        if (Objects.isNull(user)) {
            return null;
        }
        operations.set(userKey, user, 7, TimeUnit.DAYS);
        return user;
    }

    @Override
    public void deleteUserContent(Long... userId) {
        if (Objects.nonNull(userId)) {
            List<String> cacheIds = Arrays.stream(userId).map(
                    k -> StrUtil.format(RedisKeyConst.USER_INFO, k)
            ).collect(Collectors.toList());
            redisTemplate.delete(cacheIds);
        }

    }

    @Override
    public UserContext getUserContent(Long userId) {
        if (Objects.isNull(userId)) {
            return null;
        }
        ValueOperations<String, UserContext> operations = redisTemplate.opsForValue();
        String userKey = StrUtil.format(RedisKeyConst.USER_INFO, userId);
        UserContext userContext = operations.get(userKey);
        if (Objects.nonNull(userContext)) {
            return userContext;
        }
        // 获取缓存的
        return refreshUserContent(userId);
    }


    /**
     * 试用期天数
     */
    private int days = 7;

    /**
     * 创建新用户
     *
     * @param account  手机号（可为空）
     * @param username 昵称(可为空，则用手机号后四位拼接昵称，例如：小条1234)
     * @param avatar   头像（可为空）
     * @return User
     */
    private User createNewUser(String account, String username, String avatar) {
        User user = new User();
        //user.setLoginPhone(loginPhone);
        user.setMail(account);
        user.setId(IdUtil.getSnowflakeNextId());
        user.setNickName(username);
        user.setUserStatus(0);
        LocalDateTime now = LocalDateTime.now();
        user.setCreateTime(now);
        user.setIsDeleted(false);
        user.setUpdateTime(now);
        user.setIsDeleted(false);
        user.setExpireTime(now.plusDays(days));
        return user;
    }

    @Resource
    private SmsService smsService;

    @Override
    public void sendSmsCode(String mobile, Integer smsType) {
        checkMail(mobile);
        String tempId = Objects.isNull(smsType) ? String.valueOf(smsType) : String.valueOf(1);
        //String tempId = Objects.equals(smsType, 2) ? resetPasswordId : verificationCodeId;
        // 判断手机号是否验证码获取频繁
        String smsLimitKey = StrUtil.format(RedisKeyConst.SMS_CODE_REQ_LIMIT, tempId, mobile);
        if (Boolean.TRUE.equals(stringRedisTemplate.hasKey(smsLimitKey))) {
            // throw new BusinessException(RespCode.FAIL, "验证码获取过于频繁，请稍后再获取");
        }
        String smsCodeKey = StrUtil.format(RedisKeyConst.SMS_CODE, tempId, mobile);
        // 生成验证码
        String verificationCode = RandomUtil.randomNumbers(6);
        stringRedisTemplate.opsForValue().set(smsCodeKey, verificationCode, 30, TimeUnit.MINUTES);
        // 验证码获取频率
        stringRedisTemplate.opsForValue().set(smsLimitKey, "", 60, TimeUnit.SECONDS);
        // 调用阿里或腾讯短信SDK发送短信
        if (Validator.isEmail(mobile)) {
            // 发送验证码
            String subject;
            if (Objects.equals(smsType, 1)) {
                subject = "账号注册";
            } else {
                subject = "密码找回";
            }
            smsService.sendEmail(mobile, subject, subject, verificationCode);
        }
    }

}
