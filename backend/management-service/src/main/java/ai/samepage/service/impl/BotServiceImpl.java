    @Override
    public List<BotInfoVO> listBot(Integer corpId, Long userId, String botVersion,
                                   String domain, String side, HttpSession session) {
        String botDictKey = userDictBotKey(userId, botVersion, null);
        HashOperations<String, String, Object> hashOperations = redisTemplate.opsForHash();
        List<BotInfoVO> bots = (List<BotInfoVO>) hashOperations.get(botDictKey, CacheConstants.DICT_BOT_INFO_HASH_KEY);
        if (Objects.isNull(bots)) {
            // 获取bot 列表信息
            bots = baseMapper.listBot(userId);
            // 修改feature
            if (!bots.isEmpty()) {
                List<String> botKeys = bots.stream().map(BotInfoVO::getKey).collect(Collectors.toList());
                for (BotInfoVO bot : bots) {
                    List<String> feature = bot.getFeature();
                    if (CollectionUtil.isNotEmpty(feature)) {
                        if (!feature.contains("message:redirect")) {
                            continue;
                        }
                        // 判断配置是否包含有权限配置
                        List<FeatureConfigDTO> featureConfig = bot.getFeatureConfig();
                        if (featureConfig.isEmpty()) {
                            feature.remove("message:redirect");
                            continue;
                        }
                        List<FeatureConfigDTO> remove = Lists.newArrayList();
                        for (FeatureConfigDTO featureConfigDTO : featureConfig) {
                            if (Objects.equals(featureConfigDTO.getFeature(),
                                    "message:redirect") && !botKeys.contains(featureConfigDTO.getToBot())) {
                                feature.remove("message:redirect");
                                remove.add(featureConfigDTO);
                            }
                        }
                        if (!remove.isEmpty()) {
                            featureConfig.removeAll(remove);
                        }
                    }
                }
            }
            hashOperations.put(botDictKey, CacheConstants.DICT_BOT_INFO_HASH_KEY, bots);
            redisTemplate.expire(botDictKey, CacheKeyConstants.getExpireTimeSecond(EXPIRE_TIME_SECOND), TimeUnit.SECONDS);
        }
        // 编辑url
        if (StrUtil.isBlank(domain)) {
            domain = serverUrl;
        }
        String host = domain;
        if (StrUtil.isBlank(domain)) {
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "请设置请求的refer和Web-Host的值");
        }
        if (!domain.contains("/api")) {
            if (domain.endsWith("/")) {
                domain += "api";
            } else {
                domain += "/api";
            }
        } else {
            host = domain.replace("/api", "");
        }
        SupportedTypeEnum supported;
        String key = (String) session.getAttribute(Constants.TOKEN_SESSION_KEY);
        List<BotInfoVO> deleted = Lists.newArrayList();
        for (BotInfoVO bot : bots) {
            switch (side) {
                case Constants.H5_SIDE:
                    supported = bot.getH5();
                    break;
                case Constants.EXTENSION_SIDE:
                    supported = bot.getExtension();
                    break;
                default:
                case Constants.WEB_SIDE:
                    supported = bot.getWeb();
                    break;
            }
            if (Objects.nonNull(supported)) {
                if (Objects.equals(supported, SupportedTypeEnum.not_supported)) {
                    deleted.add(bot);
                    continue;
                } else if (Objects.equals(supported, SupportedTypeEnum.future_supported)) {
                    List<String> feature = bot.getFeature();
                    if (CollectionUtil.isEmpty(feature)) {
                        bot.setFeature(Collections.singletonList(supported.name().toLowerCase()));
                    } else {
                        feature.add(supported.name().toLowerCase());
                    }
                }
            }
            if (Objects.equals(bot.getType(), BotTypeEnum.external_bot)) {
                // 判断是否是外部链接
                List<FeatureConfigDTO> featureConfig = bot.getFeatureConfig();
                if (CollectionUtil.isEmpty(featureConfig)) {
                    throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR,
                            StrUtil.format("外链机器人{}的外链配置为空", bot.getName()));
                }
                for (FeatureConfigDTO configDTO : featureConfig) {
                    if (Objects.equals(configDTO.getFeature(), Constants.EXTERNAL_LINK_FEATURE_KEY)) {
                        String externalLink = configDTO.getExternalLink();
                        String url = null;
                        if (StrUtil.isBlank(externalLink)) {
                            if (Objects.isNull(key)) {
                                key = Base64Encoder.encodeUrlSafe(String.valueOf(IdUtil.getSnowflakeNextId()));
                            }
                            url = StrUtil.format("/{}/{}/key/{}/", "external", bot.getKey(), key);
                        } else if (!externalLink.startsWith("https://") && !externalLink.startsWith("http://")) {
                            if (!externalLink.startsWith("/")) {
                                externalLink = "/" + externalLink;
                            }
                            if (!externalLink.endsWith("/")) {
                                externalLink = externalLink + "/";
                            }
                            url = externalLink;
                        } else {
                            break;
                        }
                        configDTO.setExternalLink(host + url);
                        configDTO.setServerUrl(null);
                        break;
                    }
                }
            }
            if (domain.startsWith("http:")) {
                domain = domain.replace("http:", "");
            } else if (domain.startsWith("https:")) {
                domain = domain.replace("https:", "");
            } else {
                domain = "//" + domain;
            }
            bot.setSidebarIconUrl(StrUtil.format("{}/dict/sidebar/{}/{}",
                    domain, botVersion, bot.getKey())
            );
            bot.setIconUrl(StrUtil.format("{}/dict/icon/{}/{}",
                    domain, botVersion, bot.getKey()));
        }
        if (!deleted.isEmpty()) {
            bots.removeAll(deleted);
        }
        if (StrUtil.isNotBlank(key)) {
            session.setAttribute(Constants.TOKEN_SESSION_KEY, key);
            if (log.isInfoEnabled()) {
                log.info("{}失效时间：{}", StrUtil.format(Constants.TOKEN_SESSION_KEY_FM, key), session.getMaxInactiveInterval());
            }
            userTokenRedisTemplate.opsForValue().set(StrUtil.format(Constants.TOKEN_SESSION_KEY_FM, key), userId,
                    session.getMaxInactiveInterval(), TimeUnit.SECONDS);
        }
        return bots;
    }


