    @Override
    @Transactional(rollbackFor = Exception.class)
    public BaseBotInfoVO upsertBot(BotTypeEnum type,
                                   BotCommand botCommand,
                                   Long userId) {
        //
        Long id = botCommand.getId();
        LocalDateTime now = LocalDateTime.now();
        BotInfo botInfo;
        if (Objects.isNull(id)) {
            botInfo = botInfoConvert.toBotInfo(botCommand);
            botInfo.setId(IdUtil.getSnowflakeNextId());
            botInfo.setType(type);
            botInfo.setCreateTime(now);
            botInfo.setCreateId(userId);
            botInfo.setUpdateTime(now);
            WelcomeMsgVO welcome = botCommand.getWelcome();
            if (Objects.nonNull(welcome)) {
                botInfo.setWelcome(JacksonUtils.writeValueAsString(welcome));
            }
            botInfo.setSort((int) (System.currentTimeMillis() / 60000));
            botInfo.setBotType("bot");
            botInfo.setIsDeleted(false);
            botInfo.setSystem(false);
        } else {
            botInfo = getOne(
                    Wrappers.lambdaQuery(BotInfo.class)
                            .eq(BotInfo::getId, id)
                            .eq(BotInfo::getIsDeleted, false)
                            .eq(BotInfo::getCreateId, userId)
            );
            if (Objects.isNull(botInfo)) {
                throw new BusinessException("The robot does not have permission or does not exist");
            }
            botInfo.setName(botCommand.getName());
            botInfo.setDesc(botCommand.getDesc());
            botInfo.setIcon(botCommand.getIcon());
            WelcomeMsgVO welcome = botCommand.getWelcome();

            if (Objects.isNull(welcome)) {
                welcome = new WelcomeMsgVO();
            }
            botInfo.setWelcome(JacksonUtils.writeValueAsString(welcome));
        }
        if (Objects.equals(type, assistant_chat)) {
            // 文本输入
            botInfo.setFeature(JacksonUtils
                    .writeValueAsString(Collections.singletonList(text_input)));
            AssistantRequest assistantRequest = botInfoConvert.createAssistantRequest(botCommand);
            if (Objects.isNull(id)) {
                // create assistant
                userService.getUserSettings(userId);
                Assistant assistant = llmGatewayService.createAssistant(assistantRequest, userId);
                botInfo.setKey(assistant.getId());
                botInfo.setIsStream(false);
                botInfo.setExtension(SupportedTypeEnum.supported);
                botInfo.setWeb(SupportedTypeEnum.supported);
                botInfo.setH5(SupportedTypeEnum.supported);
                String categoryKey = botInfo.getCategoryKey();
                if (StrUtil.isNotBlank(categoryKey)) {
                    botInfo.setCategoryKey(categoryKey);
                } else {
                    botInfo.setCategoryKey("assistant");
                }
            } else {
                llmGatewayService.modifyAssistant(botInfo.getKey(), assistantRequest, userId);
            }
        }

        if (Objects.isNull(id)) {
            // insert
            save(botInfo);
        } else {
            update(Wrappers.lambdaUpdate(BotInfo.class)
                    .eq(BotInfo::getId, id)
                    .set(BotInfo::getUpdateTime, now)
                    .set(BotInfo::getName, botInfo.getName())
                    .set(BotInfo::getDesc, botInfo.getDesc())
                    .set(BotInfo::getIcon, botInfo.getIcon())
                    .set(BotInfo::getWelcome, botInfo.getWelcome())
            );
        }
        String botSystemVersion = getBotSystemVersion();
        redisTemplate.delete(Lists.newArrayList(StrUtil.format("dict:bot:version:{}",
                        userId),
                getBotCacheKey(id),
                userDictBotKey(userId, botSystemVersion, Constants.EXTENSION_SIDE),
                userDictBotKey(userId, botSystemVersion, Constants.WEB_SIDE),
                userDictBotKey(userId, botSystemVersion, Constants.H5_SIDE),
                getBotCacheKey(userId)));
        BaseBotInfoVO baseBotInfo = botInfoConvert.toBaseBotInfo(botInfo);
        String welcome = botInfo.getWelcome();
        if (StrUtil.isNotBlank(welcome)) {
            baseBotInfo.setWelcome(JacksonUtils.readValue(welcome, WelcomeMsgVO.class));
        }
        String feature = botInfo.getFeature();
        if (StrUtil.isNotBlank(feature)) {
            baseBotInfo.setFeature(JacksonUtils.readValueAsList(feature, String.class));
        }
        return baseBotInfo;
    }

    @Override
    public BotVO getBot(BotTypeEnum type, Long id, Long userId) {
        BotInfo botInfo = getOne(
                Wrappers.lambdaQuery(BotInfo.class)
                        .eq(BotInfo::getId, id)
                        .eq(BotInfo::getIsDeleted, false)
                        .and(q -> q.eq(BotInfo::getSystem, true).or(qw -> qw.eq(BotInfo::getCreateId, userId)))

        );
        if (Objects.isNull(botInfo)) {
            throw new BusinessException("The robot does not have permission or does not exist");
        }
        BotVO bot = botInfoConvert.toBot(botInfo);
        if (Objects.equals(type, assistant_chat)) {
            Assistant assistant = llmGatewayService.retrieveAssistant(botInfo.getKey(), userId);
            List<String> fileIds = assistant.getFileIds();
            if (CollectionUtil.isNotEmpty(fileIds)) {
                bot.setFiles(fileIds.stream()
                        .filter(StrUtil::isNotBlank)
                        .map(k ->
                                llmGatewayService.retrieveFile(k, userId)
                        ).collect(Collectors.toList()));
            }
            Assistant retrieveAssistant = llmGatewayService.retrieveAssistant(botInfo.getKey(), userId);
            bot.setInstructions(retrieveAssistant.getInstructions());
            bot.setModel(retrieveAssistant.getModel());
            bot.setTools(retrieveAssistant.getTools());
        }
        String feature = botInfo.getFeature();
        if (StrUtil.isNotBlank(feature)) {
            bot.setFeature(JacksonUtils.readValueAsList(feature, String.class));
        }
        String welcome = botInfo.getWelcome();
        if (StrUtil.isNotBlank(welcome)) {
            bot.setWelcome(JacksonUtils.readValue(welcome, WelcomeMsgVO.class));
        }
        return bot;
    }

    @Override
    public Boolean deleteBot(Long id, Long userId) {
        update(
                Wrappers.lambdaUpdate(BotInfo.class)
                        .set(BotInfo::getIsDeleted, true)
                        .set(BotInfo::getUpdateTime, LocalDateTime.now())
                        .eq(BotInfo::getId, id)
                        .eq(BotInfo::getCreateId, userId)
                        .eq(BotInfo::getIsDeleted, false)
                        .eq(BotInfo::getSystem, false)
        );
        String botSystemVersion = getBotSystemVersion();
        redisTemplate.delete(Lists.newArrayList(StrUtil.format("dict:bot:version:{}",
                        userId),
                getBotCacheKey(id),
                userDictBotKey(userId, botSystemVersion, Constants.EXTENSION_SIDE),
                userDictBotKey(userId, botSystemVersion, Constants.WEB_SIDE),
                userDictBotKey(userId, botSystemVersion, Constants.H5_SIDE),
                getBotCacheKey(userId)));
        return true;
    }


