    private static final Integer MAX_AGE = 30 * 24 * 60 * 60;

    @Override
    public void getSidebarBase64(Long id, HttpServletResponse response) throws IOException {
        String base64 = baseMapper.getIcon(id, "sidebar");
        if (StrUtil.isBlank(base64)) {
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR,
                    StrUtil.format("机器人不存在{}", id));
        }
        stream(id, base64, response);
    }

    private void stream(Long id, String text, HttpServletResponse response) throws IOException {
        String contentType = ContentType.OCTET_STREAM.getValue();
        int index = text.indexOf(";");
        if (index > 0) {
            contentType = text.substring(0, index).replace("data:", "");
        }
        int i = text.indexOf("base64,");
        if (i > 0) {
            text = text.substring(i + 7);
        }
        byte[] bytes = Base64Utils.decodeFromString(text);
        response.setStatus(200);
        response.setContentType(contentType);
        // 设置缓存
        response.setHeader("Cache-Control", "max-age=" + MAX_AGE);
        response.setHeader("Content-Disposition", "online;filename=" + id);
        response.getOutputStream().write(bytes);
    }

