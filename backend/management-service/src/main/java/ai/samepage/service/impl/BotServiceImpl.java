    @PostConstruct
    public void init() {
        // 初始化 chat line 和chat pro 的信息
        // 同步是否有 阅读全文和阅读pdf
        log.info("开始同步chat lite 和chat pro");
        List<FeatureEnum> lineFeature = Lists.newArrayList(
                text_input, input_check, intent, prompt, edit_question, change_answer);
        List<FeatureEnum> proFeature = Lists.newArrayList(text_input, input_check, prompt, edit_question, change_answer);
        if (StrUtil.isNotBlank(systemProperties.getEmbeddingModelName())
                && Objects.nonNull(systemProperties.getChatLiteMaxToken())
                && systemProperties.getChatLiteMaxToken() > 6000
        ) {
            // 有阅读全文和阅读pdf
            lineFeature.add(read_article);
            lineFeature.add(read_doc);
        }
        // 判断是否有联网检索
        if (StrUtil.isNotBlank(systemProperties.getSearchToken())) {
            if (Objects.equals(true, systemProperties.getChatLiteFunctionCall())) {
                lineFeature.add(web_search);
            }
            if (Objects.equals(true, systemProperties.getChatProFunctionCall())) {
                proFeature.add(web_search);
            }
        }
        // 更新模型配置
        String chatLineModelName = systemProperties.getChatLiteModelName();
        Integer chatLineMaxToken = systemProperties.getChatLiteMaxToken();
        BotModelDTO botModelDTO = new BotModelDTO();
        botModelDTO.setAudit(false);
        BotModelDTO.ModelInfo modelInfo = new BotModelDTO.ModelInfo();
        modelInfo.setModel(chatLineModelName);
        modelInfo.setMaxRequestToken(chatLineMaxToken);
        botModelDTO.setModel(Collections.singletonList(modelInfo));
        String line = JacksonUtils.writeValueAsString(lineFeature);
        LambdaUpdateWrapper<BotInfo> updateWrapper = Wrappers.lambdaUpdate(BotInfo.class)
                .eq(BotInfo::getKey, ChatModelEnum.chat_lite.getName())
                .set(BotInfo::getFeature, line)
                .set(BotInfo::getModelConfig, JacksonUtils.writeValueAsString(botModelDTO))
                .set(BotInfo::getUpdateTime, LocalDateTime.now());
        String chatLineName = systemProperties.getChatLiteName();
        if (StrUtil.isNotBlank(chatLineName)) {
            updateWrapper.set(BotInfo::getName, chatLineName);
        }
        String chatLineDesc = systemProperties.getChatLiteDesc();
        if (StrUtil.isNotBlank(chatLineDesc)) {
            updateWrapper.set(BotInfo::getDesc, chatLineDesc);
        }
        update(
                updateWrapper
        );
        String pro = JacksonUtils.writeValueAsString(proFeature);
        modelInfo.setModel(systemProperties.getChatProModelName());
        modelInfo.setMaxRequestToken(systemProperties.getChatProMaxToken());
        updateWrapper = Wrappers.lambdaUpdate(BotInfo.class)
                .eq(BotInfo::getKey, ChatModelEnum.chat_pro.getName())
                .set(BotInfo::getModelConfig, JacksonUtils.writeValueAsString(botModelDTO))
                .set(BotInfo::getFeature, pro)
                .set(BotInfo::getUpdateTime, LocalDateTime.now());
        String chatProName = systemProperties.getChatProName();
        String chatProDesc = systemProperties.getChatProDesc();
        if (StrUtil.isNotBlank(chatProName)) {
            updateWrapper.set(BotInfo::getName, chatProName);
        }
        if (StrUtil.isNotBlank(chatProDesc)) {
            updateWrapper.set(BotInfo::getDesc, chatLineDesc);
        }
        update(
                updateWrapper
        );
        log.info("同步完成");
        // 删除系统缓存
        redisTemplate.delete(Constants.BOT_SYS_VERSION);
    }


