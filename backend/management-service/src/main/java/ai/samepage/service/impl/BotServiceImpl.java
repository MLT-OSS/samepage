    @Override
    public BotInfo selectByKey(String key, Integer corpId, Long userId) {
        long id = Long.parseLong(key);
        String botCacheKey = getBotCacheKey(id);
        ValueOperations<String, BotInfo> valueOperations = botRedisTemplate.opsForValue();
        BotInfo botInfo = valueOperations.get(botCacheKey);
        if (Objects.nonNull(botInfo)) {
            return botInfo;
        }
        botInfo = baseMapper.selectInfo(id);
        valueOperations.set(botCacheKey, botInfo, CacheKeyConstants.getExpireTimeSecond(EXPIRE_TIME_SECOND),
                TimeUnit.SECONDS);
        return botInfo;
    }


