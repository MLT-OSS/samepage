    @Override
    public ConversationMsgVO listMessage(BotTypeEnum type,
                                         String key,
                                         Integer corpId, Long userId,
                                         long convId,
                                         Boolean all, String domain) {
        BotInfo botInfo = botService.selectByKey(key, corpId, userId);
        type = botInfo.getType();
        ConversationInfo info = conversationBotService.getConvInfo(botInfo, userId, convId);
        ConversationMsgVO conversationMsg = conversationConvert.toConversationMsg(
                conversationConvert.toConversationInfo(info));
        List<ConversationMessage> messages = conversationMessageMapper
                .listMessage(
                        type.getConversationMsgTable(),
                        userId, convId
                );
        all = Objects.equals(type, BotTypeEnum.stateless_chat) || all;
        if (!messages.isEmpty()) {
            if (Objects.equals(all, true)) {
                conversationMsg.setMessages(messages.stream().map(msg -> toMsgChat(msg, botInfo, domain)).collect(Collectors.toList()));
            } else {
                Stack<MessageChatVO> stack = new Stack<>();
                int size = messages.size();
                String lastMessageId = messages.get(size - 1).getMessageId();
                Map<String, ConversationMessage> cache = messages.stream().collect(Collectors
                        .toMap(ConversationMessage::getMessageId, k -> k));
                ConversationMessage conversationMessage = cache.get(lastMessageId);
                while (Objects.nonNull(conversationMessage)) {
                    stack.push(toMsgChat(conversationMessage, botInfo, domain));
                    String parentId = conversationMessage.getParentId();
                    if (StrUtil.isBlank(parentId)) {
                        break;
                    }
                    conversationMessage = cache.get(parentId);
                }
                List<MessageChatVO> res = Lists.newArrayListWithCapacity(stack.size());
                while (!stack.isEmpty()) {
                    res.add(stack.pop());
                }
                conversationMsg.setMessages(res);
            }
        } else {
            conversationMsg.setMessages(Collections.emptyList());
        }
        return conversationMsg;
    }

