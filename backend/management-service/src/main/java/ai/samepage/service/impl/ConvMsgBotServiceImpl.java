    @Override
    public MessageChatVO stopMsg(BotInfo botInfo, Long userId, MessageChatVO message, String domain) {
        // 放入缓存信息
        String connectId = message.getConnectId();
        String key;
        ConversationMessage convMsg = toConvMsg(message);
        convMsg.setCreateId(userId);
        if (StrUtil.isBlank(connectId)) {
            key = MessageConstants.buildStopMsgKey(connectId);
        } else {
            key = MessageConstants.buildStopMsgKey(userId, convMsg.getConversationId(), convMsg.getMessageId());
        }
        redisTemplate.opsForValue().set(
                key,
                String.valueOf(System.currentTimeMillis()),
                CacheKeyConstants.getExpireTimeSecond(3600),
                TimeUnit.SECONDS
        );
        BotTypeEnum type = botInfo.getType();
        if (Objects.equals(type, BotTypeEnum.assistant_chat)) {
            // 取消assistant 任务
            ConvMsgTaskService taskService = convMsgTaskService.get(type.name());
            if (Objects.nonNull(taskService)) {
                taskService.cancelTask(
                        type, botInfo.getKey(), userId, Long.parseLong(message.getConversationId()), message.getMessageId()
                );
            }
        }
        update(botInfo, convMsg, MessageEnum.RUNNING, MessageEnum.ASYNC_RUNNING, MessageEnum.COMPLETED, MessageEnum.SUCCESS);
        // 更新会话的描述信息

        MessageEnum status = convMsg.getStatus();
        if (Objects.equals(status, MessageEnum.SUCCESS)) {
            String desc = MessageUtil.getContent(convMsg);
            if (StrUtil.isNotBlank(desc)) {
                conversationBotService.updateDesc(
                        botInfo, convMsg.getConversationId(), userId, desc
                );
            }
        }
        return getMsg(botInfo, userId, convMsg.getConversationId(), convMsg.getMessageId(), domain);
    }


