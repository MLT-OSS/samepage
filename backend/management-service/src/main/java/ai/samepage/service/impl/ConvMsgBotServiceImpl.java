    private static final Integer UPLOAD_EXPIRE_SECOND = 30 * 60;


    private static final Integer DOWNLOAD_EXPIRE_SECOND = 2 * 60 * 60;

    @Override
    public UploadObjConfigVO getUploadConfig(String type, String md5, String suffix) {
        if (StrUtil.isBlank(md5) || StrUtil.isBlank(suffix) || StrUtil.isBlank(type)) {
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "参数不正确");
        }
        suffix = suffix.toLowerCase();
        UploadObjConfigVO res = new UploadObjConfigVO();

        String objId = StrUtil.format("{}/{}/{}.{}", webConfig.profile, type, md5, suffix);
        res.setObjId(objId);
        res.setSuffix(suffix);
        res.setBucketName(s3Config.picBucketName);
        res.setUploadUrl(pictureS3Service.uploadPresidedUrl(
                objId, UPLOAD_EXPIRE_SECOND
        ).toString());
        res.setDownloadUrl(
                pictureS3Service.downloadPresignedUrl(
                        objId, DOWNLOAD_EXPIRE_SECOND, StrUtil.format("{}/{}", type, suffix),
                        StrUtil.format("inline; filename=\"{}\";", objId), CACHE_MAX_AGE
                ).toString()
        );
        return res;
    }

    @Override
    public ConversationMessage toConvMsg(MessageChatVO message) {
        String conversationId = message.getConversationId();
        String messageId = message.getMessageId();
        if (StrUtil.isBlank(conversationId) || StrUtil.isBlank(messageId)) {
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR,
                    "会话id和消息id不能为空");
        }
        long convId = Long.parseLong(conversationId);
        ConversationMessage msg = new ConversationMessage();
        // msg.setId(convId);
        msg.setMessageId(messageId);
        msg.setSource(message.getSource());
        msg.setType(MessageTypeEnum.text);
        msg.setRole(ChatRoleEnum.assistant);
        msg.setConversationId(convId);
        MessageEnum status;
        ContentTypeEnum contentType = message.contentType();
        switch (message.getStatus()) {
            case MESSAGE_SUCCESS_STATUS:
                status = MessageEnum.SUCCESS;
                switch (contentType) {
                    case input:
                    case reply_input:
                    case web_search:
                        String content = message.content();
                        if (StrUtil.isNotBlank(content)) {
                            List<MsgDTO> msgList = JacksonUtils.readValueAsList(content, MsgDTO.class);
                            for (MsgDTO msgDTO : msgList) {
                                String type = msgDTO.getType();
                                if (StrUtil.isBlank(type)) {
                                    throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "获取不到content里面的type");
                                }
                                switch (type) {
                                    case INPUT_IMAGE_TYPE:
                                        UploadObjConfigVO obj = msgDTO.imageValue();
                                        obj.setDownloadUrl(null);
                                        obj.setUploadUrl(null);
                                        break;
                                    case INPUT_VIDEO_TYPE:
                                        obj = msgDTO.videoValue();
                                        obj.setDownloadUrl(null);
                                        obj.setUploadUrl(null);
                                        break;
                                    default:
                                        break;
                                }
                            }
                            content = JacksonUtils.writeValueAsString(msgList);
                        }
                        msg.setContent(content);
                        break;
                    default:
                        msg.setContent(message.content());
                        break;
                }
                break;
            case MESSAGE_FAILED_STATUS:
                status = MessageEnum.CLIENT_FAILED;
                msg.setContent(message.content());
                break;
            default:
                throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR,
                        StrUtil.format("不支持消息的状态{}", message.getStatus()));
        }
        msg.setStatus(status);
        msg.setContentType(contentType);
        msg.setIsDeleted(false);
        msg.setLike(MessageLikeEnum.unknown.name());
        List<MessageChatVO.TextVO> reference = message.getReference();
        if (Objects.nonNull(reference)) {
            msg.setReference(JacksonUtils.writeValueAsString(reference));
        }
        msg.setParentId(message.getParentMessageId());
        msg.setType(MessageTypeEnum.text);
        msg.setQuestion(message.getQuestion());
        msg.setUpdateTime(LocalDateTime.now());
        List<MessageChatVO.TextVO> suggest = message.getSuggest();
        if (Objects.nonNull(suggest)) {
            msg.setSuggest(JacksonUtils.writeValueAsString(suggest));
        }
        return msg;
    }

    @Override
    public MessageChatVO toMsgChat(ConversationMessage msg, BotInfo botInfo, String domain) {
        MessageEnum message = msg.getStatus();
        if (Objects.equals(message, MessageEnum.ASYNC_RUNNING)) {
            msg.setStatus(MessageEnum.RUNNING);
        }
        MessageChatVO messageChat = conversationConvert.toMessageChat(msg);
        // 后置处理
        String content = msg.getContent();
        if (StrUtil.isBlank(content)) {
            content = msg.getLongContent();
        }
        String status = messageChat.getStatus();
        if (Objects.equals(status, MESSAGE_SUCCESS_STATUS)
                || Objects.equals(status, MESSAGE_TOKEN_EXCEEDING_STATUS)
        ) {
            if (StrUtil.isNotBlank(content)) {
                ContentTypeEnum contentType = messageChat.getContentType();
                switch (contentType) {
                    case input:
                    case reply_input:
                        String templateConfig = botInfo.getTemplateConfig();
                        Map<String, TemplateConfigDto> cache = Collections.emptyMap();
                        if (StrUtil.isNotBlank(templateConfig)) {
                            cache = JacksonUtils.readValueAsList(templateConfig, TemplateConfigDto.class)
                                    .stream().collect(Collectors.toMap(TemplateConfigDto::getKey, k -> k));
                        }
                        List<MsgDTO> msgList = JacksonUtils.readValueAsList(content, MsgDTO.class);
                        for (MsgDTO msgDTO : msgList) {
                            String value = msgDTO.getValue();
                            switch (msgDTO.getType()) {
                                case INPUT_VIDEO_TYPE:
                                    UploadObjConfigVO uploadObjConfigVO = msgDTO.videoValue();
                                    String objId = uploadObjConfigVO.getObjId();
                                    String suffix = uploadObjConfigVO.getSuffix();
                                    String urlStr = S3Config.getByBucket(uploadObjConfigVO.getBucketName())
                                            .downloadPresignedUrl(
                                                    objId,
                                                    Constants.DOWNLOAD_URL_EXPIRE_SECOND,
                                                    StrUtil.format("video/{}", suffix),
                                                    StrUtil.format("inline; filename=\"{}.{}\";", objId, suffix), CACHE_MAX_AGE
                                            ).toString();
                                    if (!Objects.equals(s3Config.docExternal, true) && StrUtil.isNotBlank(domain)) {
                                        String oldDomain = SessionUtil.getDomainByUrl(urlStr);
                                        if (StrUtil.isNotBlank(oldDomain)) {
                                            urlStr = urlStr.replace(oldDomain, domain);
                                        }
                                    }
                                    uploadObjConfigVO.setDownloadUrl(
                                            urlStr
                                    );
                                    value = JacksonUtils.writeValueAsString(uploadObjConfigVO);
                                    break;
                                case INPUT_IMAGE_TYPE:
                                    uploadObjConfigVO = msgDTO.imageValue();
                                    objId = uploadObjConfigVO.getObjId();
                                    suffix = uploadObjConfigVO.getSuffix();
                                    urlStr = S3Config.getByBucket(uploadObjConfigVO.getBucketName())
                                            .downloadPresignedUrl(
                                                    objId,
                                                    Constants.DOWNLOAD_URL_EXPIRE_SECOND,
                                                    StrUtil.format("video/{}", suffix),
                                                    StrUtil.format("inline; filename=\"{}.{}\";", objId, suffix), CACHE_MAX_AGE
                                            ).toString();
                                    if (!Objects.equals(s3Config.docExternal, true) && StrUtil.isNotBlank(domain)) {
                                        String oldDomain = SessionUtil.getDomainByUrl(urlStr);
                                        if (StrUtil.isNotBlank(oldDomain)) {
                                            urlStr = urlStr.replace(oldDomain, domain);
                                        }
                                    }
                                    uploadObjConfigVO.setDownloadUrl(
                                            urlStr
                                    );
                                    value = JacksonUtils.writeValueAsString(uploadObjConfigVO);
                                    break;
                                case INPUT_TEMPLATE_TYPE:
                                    TemplateConfigDto templateConfigDto = msgDTO.templateValue();
                                    TemplateConfigDto template = cache.get(templateConfigDto.getKey());
                                    if (Objects.nonNull(template)) {
                                        templateConfigDto.setName(template.getName());
                                    } else if (StrUtil.isBlank(templateConfigDto.getName())) {
                                        templateConfigDto.setName(templateConfigDto.getKey());
                                    }
                                    value = JacksonUtils.writeValueAsString(templateConfigDto);
                                    break;
                                default:
                                    break;
                            }
                            msgDTO.setValue(value);
                        }
                        if (CollectionUtil.isNotEmpty(msgList)) {
                            messageChat.setContent(JacksonUtils.writeValueAsString(msgList));
                        }
                        break;
                    default:
                        messageChat.setContent(content);
                        break;
                }
            }
        }
        return messageChat;
    }

