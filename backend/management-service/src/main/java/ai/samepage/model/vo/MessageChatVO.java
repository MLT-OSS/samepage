package ai.samepage.model.vo;

import ai.samepage.entity.ChatRoleEnum;
import ai.samepage.entity.ContentTypeEnum;
import ai.samepage.entity.MessageLikeEnum;
import ai.samepage.entity.MessageTypeEnum;
import ai.samepage.model.MessageEnum;
import com.fasterxml.jackson.annotation.JsonFormat;
import io.swagger.annotations.ApiModelProperty;
import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;

import java.time.LocalDateTime;
import java.util.List;
import java.util.Objects;


/**
 * 会话的消息信息
 *
 */
@Data
@AllArgsConstructor
@NoArgsConstructor
public class MessageChatVO {

    @ApiModelProperty("message的唯一标识")
    private String messageId;

    @ApiModelProperty("会话的唯一标识")
    private String conversationId;


    private String connectId;


    @ApiModelProperty("内容信息，对于特殊的contentType 结果是json字符串")
    private String content;

    public String getContent() {
        if (Objects.equals(role, ChatRoleEnum.user)) {
            return content;
        }
        if (Objects.isNull(status) || Objects.equals(status.isError(), true)) {
            if (Objects.isNull(status)) {
                status = MessageEnum.SERVER_ERROR_CHINESE;
            }
            return status.getDesc();
        }
        return content;
    }

    public String content() {
        return this.content;
    }


    public ContentTypeEnum contentType() {
        return contentType;
    }


    @ApiModelProperty("消息标题")
    private String title;
    @ApiModelProperty(value = "创建时间")
    @JsonFormat(pattern = "yyyy-MM-dd HH:mm:ss")
    private LocalDateTime createTime;

    @ApiModelProperty(value = "message的类型,即文本或者图文", allowableValues = "text")
    private MessageTypeEnum type;


    @ApiModelProperty(value = "内容的类型，整体上分为三类：文本，快捷操作，阅读全文,摘要及可能问到的问题,带有来源的信息", allowableValues = "text")
    private ContentTypeEnum contentType;


    public ContentTypeEnum getContentType() {
        if (Objects.equals(this.role, ChatRoleEnum.user)) {
            return contentType;
        }
        if (Objects.isNull(status)) {
            return ContentTypeEnum.text;
        }
        if (Objects.equals(status.isError(), true)) {
            return ContentTypeEnum.text;
        }
        return contentType;
    }

    @ApiModelProperty(value = "角色信息")
    private ChatRoleEnum role;


    @ApiModelProperty(value = "创建者id")
    private Long createId;

    private String source;


    private String question;

    @ApiModelProperty(value = "快捷操作的id,一个快捷操作有相同id，当contentType为快捷操作时才会有值")
    private String intentId;


    @ApiModelProperty(value = "是否展示")
    private Boolean isShow;

    public Boolean getIsShow() {
        if (Objects.nonNull(this.contentType)) {
            return this.contentType.isShow();
        }
        return true;
    }


    public String getStatus() {
        if (Objects.equals(role, ChatRoleEnum.user)) {
            return MessageEnum.SUCCESS.getStatus();
        }
        if (Objects.isNull(status)) {
            return MessageEnum.SERVER_ERROR_CHINESE.getStatus();
        }
        return status.getStatus();
    }


    @ApiModelProperty(value = "可能提到的问题列表，当contentType为摘要及可能问到的问题才会有值，" +
            "并且这里是感兴趣的问题列表")
    private List<TextVO> suggest;


    @ApiModelProperty(value = "来源信息")
    private List<TextVO> reference;


    @ApiModelProperty(value = "反馈信息，当值为空时未设置，值的含义是赞或者踩或没有设置",
            allowableValues = "like,dislike,")
    private MessageLikeEnum like;


    @ApiModelProperty("父的messageId")
    private String parentMessageId;


    /* @ApiModelProperty("子的messageId")
     private List<String> children;
 */
    @ApiModelProperty("状态是否成功")
    private MessageEnum status;


    @Data
    @AllArgsConstructor
    @NoArgsConstructor
    public static class TextVO {

        @ApiModelProperty("唯一标识,这是为了流式返回时区分每一条消息的信息，特意冗余的字段")
        private String key;

        @ApiModelProperty("内容")
        private String text;


        private String type;


        private List<Integer> pos;


        private List<Integer> position;

        private String link;

        private String title;

        public TextVO(String key, String text) {
            this.key = key;
            this.text = text;
        }


        public List<Integer> getPosition() {
            return pos;
        }
    }
}
