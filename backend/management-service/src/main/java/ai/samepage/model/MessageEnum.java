package ai.samepage.model;

import ai.samepage.constant.MessageConstants;
import ai.samepage.entity.ChatLanguageEnum;
import cn.hutool.core.util.StrUtil;
import com.baomidou.mybatisplus.annotation.EnumValue;

import java.util.Objects;

import static ai.samepage.constant.MessageConstants.MESSAGE_FAILED_STATUS;


/**
 * 消息枚举信息
 *
 */

public enum MessageEnum {


    /**
     * 正在运行中
     */
    RUNNING(-100, MessageConstants.MESSAGE_RUNNING_STATUS, MessageConstants.MESSAGE_RUNNING, null, false),

    /**
     * 正在运行中
     */
    ASYNC_RUNNING(-50, MessageConstants.MESSAGE_RUNNING_STATUS, MessageConstants.MESSAGE_RUNNING, null, false),


    COMPLETED(-1, "COMPLETED", "任务已完成", null, false),

    /**
     * 成功
     */
    SUCCESS(0, MessageConstants.MESSAGE_SUCCESS_STATUS, null, null, false),

    /**
     * 客户端主动报的错
     */
    CLIENT_FAILED(100, MessageConstants.CLIENT_FAILED_STATUS, null, null, MESSAGE_FAILED_STATUS),

    SERVER_ERROR_CHINESE(101, MESSAGE_FAILED_STATUS, "请求失败，请重试!",
            ChatLanguageEnum.Chinese, MESSAGE_FAILED_STATUS),


    SERVER_ERROR_ENGLISH(102, MESSAGE_FAILED_STATUS,
            "Request failed, please try again later!",
            ChatLanguageEnum.English, MESSAGE_FAILED_STATUS),

    FAILED(null, MESSAGE_FAILED_STATUS, null, null, true),


    LIMITED_ERROR_CHINESE(104, MessageConstants.EXCEED_MESSAGE_TYPE,
            "系统繁忙，请稍后再试!",
            ChatLanguageEnum.Chinese, MESSAGE_FAILED_STATUS),

    LIMITED_ERROR_ENGLISH(103, MessageConstants.EXCEED_MESSAGE_TYPE,
            "System busy, please try again later!",
            ChatLanguageEnum.English, MESSAGE_FAILED_STATUS),

    EXCEED_TOKEN(109, MessageConstants.MESSAGE_TOKEN_EXCEEDING_STATUS,
            null,
            null, false);


    @EnumValue
    private final Integer code;

    private final String type;

    private final String status;

    private final String desc;


    private final ChatLanguageEnum language;

    private final boolean isError;


    MessageEnum(Integer code, String type, String desc, ChatLanguageEnum language, String status) {
        this.code = code;
        this.type = type;
        this.desc = desc;
        this.language = language;
        this.isError = true;
        this.status = status;
    }

    MessageEnum(Integer code, String type, String desc,
                ChatLanguageEnum language, boolean isError) {
        this.code = code;
        this.type = type;
        this.desc = desc;
        this.language = language;
        this.isError = isError;
        this.status = type;
    }


    public String getStatus() {
        return status;
    }

    public Integer getCode() {
        return code;
    }

    public String getType() {
        return type;
    }

    public String getDesc() {
        return desc;
    }

    public ChatLanguageEnum getLanguage() {
        return language;
    }


    public boolean isError() {
        return isError;
    }

    public static MessageEnum getMessage(String type, ChatLanguageEnum language) {
        if (Objects.equals(language, ChatLanguageEnum.Auto)) {
            language = ChatLanguageEnum.Chinese;
        }
        for (MessageEnum value : values()) {
            if (Objects.equals(type, value.type) && Objects.equals(language, value.language)) {
                return value;
            }
        }
        return null;

    }

    public static MessageEnum getMessage(String type, String language) {
        if (StrUtil.isNotBlank(language)) {
            language = language.toLowerCase();
        }
        MessageEnum defaultValue = null;
        for (MessageEnum value : values()) {
            if (Objects.equals(type, value.type)) {
                if (Objects.isNull(language)) {
                    return value;
                }
                ChatLanguageEnum lang = value.language;
                if (Objects.isNull(defaultValue) && Objects.isNull(lang)) {
                    defaultValue = value;
                }
                if (Objects.isNull(lang)){
                    return value;
                }
                if (language.equalsIgnoreCase(lang.name())) {
                    return value;
                }
            }
        }
        return defaultValue;

    }

    public static MessageEnum getMessage(Integer code) {
        for (MessageEnum value : values()) {
            if (Objects.equals(code, value.code)) {
                return value;
            }
        }
        return null;
    }


    public static String getErrorMessage(Integer code) {
        for (MessageEnum value : values()) {
            if (Objects.equals(code, value.code)) {
                if (value.isError) {
                    return value.getDesc();
                } else {
                    return null;
                }
            }
        }
        return MessageEnum.SERVER_ERROR_CHINESE.getDesc();
    }


}
