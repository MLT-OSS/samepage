    @Resource
    private ConvMsgBotService convMsgBotService;

    /**
     * 1. sse 发送消息，流式和非流式
     * 2. 中断消息信息
     * 3. 获取上传的基本信息
     */
    @PostMapping("/stream/conv/{key}/msg")
    @ApiOperation(value = "流式输入消息", produces = "text/event-stream;charset=UTF-8")
    public SseEmitter streamChat(@RequestBody ConversationMsgCommand command,
                                 @PathVariable("key") String key,
                                 @ApiIgnore HttpServletRequest request,
                                 @ApiIgnore HttpServletResponse response) {
        UserContext userContext = UserContextHelper.get();
        response.setHeader("X-Accel-Buffering", "no");
        return conversationInfoService.streamChat(
                key, userContext.getUserId(),
                userContext.getTenantId(),
                command,
                request.getHeader(HttpHeaders.ACCEPT_LANGUAGE)
        );
    }

    /**
     * 1. sse 发送消息，流式和非流式
     * 2. 中断消息信息
     * 3. 获取上传的基本信息
     */
    @GetMapping("/stream/msg/{key}/{convId}/{msgId}")
    @ApiOperation(value = "流式输入消息", produces = "text/event-stream;charset=UTF-8")
    public SseEmitter streamChat(@PathVariable("key") String key,
                                 @PathVariable("convId") Long convId,
                                 @PathVariable("msgId") String msgId,
                                 @ApiIgnore HttpServletResponse response) {
        UserContext userContext = UserContextHelper.get();
        response.setHeader("X-Accel-Buffering", "no");
        return conversationInfoService.streamMsg(
                key,
                userContext.getUserId(),
                convId,
                msgId
        );
    }

    @PostMapping("/conv/{key}/msg")
    @ApiOperation(value = "非流式输入消息")
    public RespBody<MessageChatVO> chat(@RequestBody ConversationMsgCommand command,
                                        @PathVariable("key") String key,
                                        @ApiIgnore HttpServletRequest request) {
        UserContext userContext = UserContextHelper.get();
        return RespBody.ok(conversationInfoService.chat(
                key, userContext.getUserId(),
                userContext.getTenantId(),
                command,
                request.getHeader(HttpHeaders.ACCEPT_LANGUAGE),
                SessionUtil.getDomain(request)
        ));
    }

    @PostMapping("/conv/{key}/stop")
    @ApiOperation(value = "终止响应")
    public RespBody<MessageChatVO> chatMsgStop(@PathVariable("key") String key,
                                               @RequestBody MessageChatVO message,
                                               HttpServletRequest request) {
        UserContext userContext = UserContextHelper.get();
        Long userId = userContext.getUserId();
        return RespBody.ok(convMsgBotService.stopMsg(
                botService.selectByKey(key, userContext.getTenantId(), userId), userId,
                message, SessionUtil.getDomain(request)));
    }

    /**
     * 获取上传的基本信息
     */
    @ApiOperation(value = "获取上传的地址等其他信息")
    @GetMapping("/conv/upload/config")
    public RespBody<UploadObjConfigVO> uploadConfig(
            @RequestParam("type") String type,
            @RequestParam("suffix") String suffix,
            @RequestParam("size") Integer size,
            @RequestParam("md5") String md5,
            @RequestParam("fileName") String fileName

    ) {
        UploadObjConfigVO uploadObjConfig = convMsgBotService.getUploadConfig(
                type, md5, suffix
        );
        uploadObjConfig.setFileName(fileName);
        uploadObjConfig.setSize(size);
        return RespBody.ok(uploadObjConfig);
    }

    @PostMapping("/conv/upload/exists")
    public RespBody<Boolean> uploadConfig(
            @RequestBody UploadObjConfigVO config

    ) throws InterruptedException {
        String objId = config.getObjId();
        String bucketName = config.getBucketName();
        if (StrUtil.isBlank(objId) || StrUtil.isBlank(bucketName)) {
            return RespBody.fail("参数不支持");
        }
        IS3Service is3Service = S3Config.getByBucket(bucketName);
        if (Objects.isNull(is3Service)) {
            return RespBody.fail(StrUtil.format("找不到{}客户端", bucketName));
        }
        for (int i = 0; i < 100; i++) {
            if (is3Service.existsObj(objId)) {
                return RespBody.ok(true);
            }
            Thread.sleep(100L);
        }
        return RespBody.ok(false);
    }

