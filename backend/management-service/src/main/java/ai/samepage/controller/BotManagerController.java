package ai.samepage.controller;


import ai.samepage.common.core.base.entity.BaseResponse;
import ai.samepage.common.core.base.entity.UserContext;
import ai.samepage.common.core.helper.UserContextHelper;
import ai.samepage.common.core.resp.RespBody;
import ai.samepage.common.core.resp.RespCode;
import ai.samepage.common.core.util.JacksonUtils;
import ai.samepage.entity.BotInfo;
import ai.samepage.entity.BotTypeEnum;
import ai.samepage.model.command.BotCommand;
import ai.samepage.model.vo.BaseBotInfoVO;
import ai.samepage.model.vo.BotVO;
import ai.samepage.model.vo.WelcomeMsgVO;
import ai.samepage.service.BotService;
import ai.samepage.service.BotUserService;
import cn.hutool.core.util.RandomUtil;
import cn.hutool.core.util.StrUtil;
import com.google.common.collect.Lists;
import io.swagger.annotations.Api;
import io.swagger.annotations.ApiImplicitParam;
import io.swagger.annotations.ApiImplicitParams;
import io.swagger.annotations.ApiOperation;
import org.springframework.web.bind.annotation.*;

import javax.annotation.Resource;
import javax.servlet.http.HttpServletRequest;
import java.util.List;
import java.util.Objects;

/**
 * Bot Manager
 */
@Api(tags = "机器人相关接口")
@RestController
public class BotManagerController {


    @Resource
    private BotService botService;


    @ApiOperation(value = "获取钉在侧边的机器人列表")
    @GetMapping("/bot/sidebar")
    public RespBody<List<String>> sidebar(HttpServletRequest request) {
        UserContext userContext = UserContextHelper.get();
        return RespBody.ok(botUserService.sidebar(userContext.getTenantId(), userContext.getUserId(), request));
    }



    @Resource
    private BotUserService botUserService;

    @ApiOperation(value = "将某个机器人钉在用户侧边上")
    @GetMapping("/bot/fixed/{key}")
    public RespBody<Boolean> botSidebar(
            @PathVariable("key") String key,
            @RequestParam("fixed") Boolean sidebar
    ) {
        UserContext userContext = UserContextHelper.get();
        return RespBody.ok(botUserService.upsertSidebar(userContext.getUserId(),
                userContext.getTenantId(), key, sidebar));
    }


    @ApiOperation(value = "获取一个机器人的会话欢迎语")
    @ApiImplicitParams(
            {
                    @ApiImplicitParam(name = "key", required = true, value = "机器人的唯一标识")
            }
    )
    @GetMapping("/bot/{key}/welcome")
    public RespBody<WelcomeMsgVO> welcome(@PathVariable("key") String key,
                                          @RequestParam(name = "value", defaultValue = "3", required = false) Integer size) {
        UserContext userContext = UserContextHelper.get();
        BotInfo botInfo = botService.selectByKey(key, userContext.getTenantId(), userContext.getUserId());
        String welcome = botInfo.getWelcome();
        if (StrUtil.isBlank(welcome)) {
            return RespBody.ok(new WelcomeMsgVO());
        } else {
            WelcomeMsgVO welcomeMsgVO = JacksonUtils.readValue(welcome, WelcomeMsgVO.class);
            List<String> questions = welcomeMsgVO.getQuestions();
            if (Objects.nonNull(questions) && questions.size() > size) {
                List<String> ques = Lists.newArrayList();
                while (ques.size() < size) {
                    int i = RandomUtil.randomInt(0, questions.size());
                    String s = questions.get(i);
                    if (!ques.contains(s)) {
                        ques.add(s);
                    }
                }
                welcomeMsgVO.setQuestions(ques);
            }
            return RespBody.ok(welcomeMsgVO);
        }
    }


    @GetMapping("/bots")
    public RespBody<BaseResponse<BaseBotInfoVO>> bot(@RequestParam(value = "pageNo", defaultValue = "1") Integer pageNo,
                                                     @RequestParam(value = "pageSize", defaultValue = "10") Integer pageSize,
                                                     @RequestParam(value = "keyword", required = false) String keyword,
                                                     @RequestParam(value = "category", required = false) String category,
                                                     @RequestParam(value = "type") BotTypeEnum botType,
                                                     HttpServletRequest request
    ) {
        return RespBody.ok(botService.listBots(UserContextHelper.get(), botType, keyword, category, pageSize, pageNo));
    }

    @PostMapping("/bot/{type}")
    public RespBody<BaseBotInfoVO> addBot(@PathVariable("type") BotTypeEnum type, @RequestBody BotCommand botCommand) {
        return RespBody.ok(botService.upsertBot(type, botCommand, UserContextHelper.get().getUserId()));
    }

    @PutMapping("/bot/{type}")
    public RespBody<BaseBotInfoVO> updateBot(@PathVariable("type") BotTypeEnum type, @RequestBody BotCommand botCommand) {
        Long id = botCommand.getId();
        if (Objects.isNull(id)){
            return RespBody.fail(RespCode.TRANSFER_EXCEPTION.getMessage());
        }
        return RespBody.ok(botService.upsertBot(type, botCommand, UserContextHelper.get().getUserId()));
    }

    @GetMapping("/bot/{type}")
    public RespBody<BotVO> getBot(
            @PathVariable("type") BotTypeEnum type,
            @RequestParam("id") Long id
    ) {
        return RespBody.ok(botService.getBot(type, id, UserContextHelper.get().getUserId()));
    }

    @DeleteMapping("/bot/{id}")
    public RespBody<Boolean> deleteBot(
            @PathVariable("id") Long id
    ) {
        return RespBody.ok(botService.deleteBot(id, UserContextHelper.get().getUserId()));
    }
}
