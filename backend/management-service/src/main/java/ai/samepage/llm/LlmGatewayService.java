package ai.samepage.llm;

import ai.samepage.common.core.exception.BusinessException;
import ai.samepage.common.core.resp.RespCode;
import ai.samepage.common.core.util.JacksonUtils;
import ai.samepage.llm.exception.OpenAiError;
import ai.samepage.llm.exception.OpenAiHttpException;
import ai.samepage.llm.model.*;
import cn.hutool.core.util.StrUtil;
import com.google.common.collect.Maps;
import io.reactivex.rxjava3.core.BackpressureStrategy;
import io.reactivex.rxjava3.core.Flowable;
import io.reactivex.rxjava3.core.Single;
import lombok.extern.slf4j.Slf4j;
import okhttp3.*;
import retrofit2.Call;
import retrofit2.HttpException;
import retrofit2.Retrofit;
import retrofit2.adapter.rxjava3.RxJava3CallAdapterFactory;
import retrofit2.converter.jackson.JacksonConverterFactory;

import java.io.IOException;
import java.lang.Thread;
import java.time.Duration;
import java.util.Collections;
import java.util.List;
import java.util.Map;
import java.util.Objects;
import java.util.concurrent.ExecutorService;
import java.util.concurrent.TimeUnit;

/**
 * Unified OpenAI Assistant API interface style docking service
 */

@Slf4j
public class LlmGatewayService {


    private final OpenAiApi openAiApi;


    public LlmGatewayService(String baseUrl, String token, Duration timeout, ExecutorService executorService) {
        //声明日志类
       // HttpLoggingInterceptor httpLoggingInterceptor = new HttpLoggingInterceptor();
//设定日志级别
       // httpLoggingInterceptor.setLevel(HttpLoggingInterceptor.Level.BODY);
        // 1.初始化 OkHttpClient
        OkHttpClient okHttpClient = new OkHttpClient.Builder()
                //.dispatcher(new Dispatcher(executorService))
                .addInterceptor(new AuthenticationInterceptor(token))
               // .addInterceptor(httpLoggingInterceptor)
                .connectionPool(new ConnectionPool(5, 1, TimeUnit.SECONDS))
                .readTimeout(timeout.toMillis(), TimeUnit.MILLISECONDS)
                .build();

        //  2. 构建 retrofit
        Retrofit retrofit = new Retrofit.Builder()
                .baseUrl(baseUrl)
                .client(okHttpClient)
                .callbackExecutor(executorService)
                .addConverterFactory(JacksonConverterFactory.create(JacksonUtils.getObjectMapper()))
                .addCallAdapterFactory(RxJava3CallAdapterFactory.create())
                .build();
        this.openAiApi = retrofit.create(OpenAiApi.class);
    }

    /**
     * Cancels a run with the specified thread ID and runs ID.
     *
     * @param threadId The ID of the thread.
     * @param runsId   The ID of the run to be cancelled.
     * @return A Run object representing the result of the cancellation.
     */
    public Run cancelRun(String threadId, String runsId, Long userId) {
        return execute(openAiApi.cancelRun(threadId, runsId, String.valueOf(userId)));
    }

    /**
     * Creates an Assistant.
     *
     * @param request The AssistantRequest object containing the request details.
     * @return The created Assistant object.
     */
    public Assistant createAssistant(AssistantRequest request, Long userId) {
        return execute(openAiApi.createAssistant(request, String.valueOf(userId)));
    }


    /**
     * Modifies an assistant's information.
     *
     * @param assistantId The unique identifier of the assistant.
     * @param request     The AssistantRequest object containing the modifications to be made.
     * @return The modified Assistant object.
     */
    public Assistant modifyAssistant(String assistantId, AssistantRequest request, Long userId) {
        return execute(openAiApi.modifyAssistant(assistantId, request, String.valueOf(userId)));
    }


    /**
     * Retrieves a list of assistants.
     *
     * @param cursorAssistantId The cursor assistant ID for iteration.
     * @param limit             The limit on the number of returned items.
     * @return OpenAiResponse<Assistant> A response object containing the list of assistants.
     */
    public OpenAiResponse<Assistant> listAssistants(String cursorAssistantId,
                                                    Integer limit, Long userId) {
        Map<String, Object> params = Maps.newHashMap();
        params.put("order", "desc");
        params.put("limit", limit);
        if (StrUtil.isNotBlank(cursorAssistantId)) {
            params.put("after", cursorAssistantId);
        }
        return execute(openAiApi.listAssistants(params, String.valueOf(userId)));
    }

    public <T> T execute(Single<T> apiCall) {
        try {
            log.info("放入token的线程id:{}", Thread.currentThread().getId());
            return apiCall.blockingGet();
        } catch (HttpException e) {
            try {
                if (e.response() == null || e.response().errorBody() == null) {
                    throw e;
                }
                String errorBody = e.response().errorBody().string();

                OpenAiError error = JacksonUtils.readValue(errorBody, OpenAiError.class);
                throw new OpenAiHttpException(error, e, e.code());
            } catch (IOException ex) {
                // couldn't parse OpenAI error
                throw e;
            }
        }
    }

    /**
     * Retrieves an assistant based on the provided assistant ID.
     *
     * @param assistantId The unique identifier of the assistant.
     * @return The Assistant object representing the requested assistant's information.
     */
    public Assistant retrieveAssistant(String assistantId, Long userId) {
        return execute(openAiApi.retrieveAssistant(assistantId, String.valueOf(userId)));
    }


    /**
     * Retrieves a file based on the given file ID.
     *
     * @param fileId The unique identifier of the file.
     * @return A File object representing the retrieved file.
     */
    public File retrieveFile(String fileId, Long userId) {
        return execute(openAiApi.retrieveFile(fileId, String.valueOf(userId)));
    }


    /**
     * Creates a thread.
     *
     * @param messages A list of message requests.
     * @param metadata The metadata map.
     * @return The ID of the newly created thread.
     */
    public String createThread(List<AssistantMessageRequest> messages, Map<String, String> metadata, Long userId) {
        ThreadRequest threadRequest = new ThreadRequest();
        threadRequest.setMessages(messages);
        threadRequest.setMetadata(metadata);
        return execute(this.openAiApi.createThread(threadRequest, String.valueOf(userId))).getId();
    }


    /**
     * Creates a message.
     *
     * @param threadId       Thread ID
     * @param messageRequest Message request object
     * @return Message ID
     * @throws BusinessException Exception thrown when the assistant service's message creation parameters are illegal
     */
    public String createMessage(String threadId, AssistantMessageRequest messageRequest, Long userId) {
        if (StrUtil.isBlank(threadId) || Objects.isNull(messageRequest) || Objects.isNull(userId)) {
            throw new BusinessException("The assistant service's creation of message parameters is illegal");
        }
        AssistantMessage message = execute(openAiApi.createMessage(threadId, messageRequest, String.valueOf(userId)));
        if (Objects.nonNull(message)) {
            return message.getId();
        }
        throw new BusinessException(StrUtil.format(
                "The creation of a message (threadId:{},message:{}) for the assistant service failed",
                threadId, messageRequest.getContent()));
    }


    /**
     * Creates a run instance.
     *
     * @param assistantId The ID of the assistant.
     * @param threadId    The ID of the thread.
     * @return The ID of the created run instance.
     * @throws BusinessException If the parameters for creating runs in the assistant service are illegal, this exception is thrown.
     */
    public String createRuns(String assistantId, String threadId, Long userId) {
        if (StrUtil.isBlank(threadId) || StrUtil.isBlank(assistantId) || Objects.isNull(userId)) {
            throw new BusinessException(RespCode.INTERNAL_SERVER_ERROR, "The creation parameters for runs in the assistant service are illegal");
        }
        RunCreateRequest runCreateRequest = new RunCreateRequest();
        runCreateRequest.setAssistantId(assistantId);
        runCreateRequest.setTools(Collections.emptyList());
        Run run = execute(openAiApi.createRun(threadId, runCreateRequest, String.valueOf(userId)));
        if (Objects.nonNull(run)) {
            return run.getId();
        }
        throw new BusinessException(StrUtil.format(
                "Failed to create a run (assistantId:{},threadId:{}) for the assistant service",
                assistantId, threadId));
    }


    /**
     * Retrieves the run statuses for a specified thread ID and run ID.
     *
     * @param threadId Thread ID
     * @param runsId   Run ID
     * @return A list of run step objects representing the run statuses
     * @throws BusinessException If the provided runs parameters are illegal, throws a business exception
     */
    public List<RunStep> listRunsStatus(String threadId, String runsId, Long userId) {
        if (StrUtil.isBlank(threadId) || StrUtil.isBlank(runsId) || Objects.isNull(userId)) {
            throw new BusinessException("The runs parameter obtained by the assistant service is illegal");
        }
        // todo Temporarily generate up to 100 entries
        Map<String, String> params = Maps.newHashMap();
        params.put("limit", String.valueOf(100));
        params.put("order", "desc");
        OpenAiResponse<RunStep> response = execute(openAiApi.listRunSteps(
                threadId, runsId, params, String.valueOf(userId)
        ));
        if (Objects.nonNull(response)) {
            return response.getData();
        }
        throw new BusinessException(StrUtil.format(
                "Failed to obtain basic information about runs. (threadId:{},runsId:{}",
                threadId, runsId));
    }


    /**
     * Retrieves a Run object based on the given thread ID and run ID.
     *
     * @param threadId The unique identifier of the thread
     * @param runId    The unique identifier of the run
     * @return The Run object
     */
    public Run getRun(String threadId, String runId, Long userId) {
        if (StrUtil.isBlank(threadId) || StrUtil.isBlank(runId) || Objects.isNull(userId)) {
            throw new BusinessException("The message stream parameter obtained by the assistant service is illegal");
        }
        return execute(openAiApi.retrieveRun(threadId, runId, String.valueOf(userId)));
    }

    /**
     * Retrieves the assistant chat message stream.
     *
     * @param threadId The ID of the discussion thread.
     * @param runsId   The unique identifier for the run.
     * @return A Flowable emitting ChatCompletionChunk instances representing the chat message stream.
     */
    public Flowable<CompletionChunk> messageStream(String threadId, String runsId, Long userId) {
        if (StrUtil.isBlank(threadId) || StrUtil.isBlank(runsId) || Objects.isNull(userId)) {
            throw new BusinessException("The message stream parameter obtained by the assistant service is illegal");
        }
        return stream(openAiApi.messageStream(threadId, runsId, String.valueOf(userId)), CompletionChunk.class);
    }


    /**
     * Calls the Open AI api and returns a Flowable of type T for streaming
     * omitting the last message.
     *
     * @param apiCall The api call
     * @param cl      Class of type T to return
     */
    public <T> Flowable<T> stream(Call<ResponseBody> apiCall, Class<T> cl) {
        log.info("放入token的线程id:{}", Thread.currentThread().getId());
        return stream(apiCall).map(sse -> JacksonUtils.readValue(sse.getData(), cl));
    }


    /**
     * Calls the Open AI api and returns a Flowable of SSE for streaming
     * omitting the last message.
     *
     * @param apiCall The api call
     */
    public Flowable<SSE> stream(Call<ResponseBody> apiCall) {
        return stream(apiCall, false);
    }

    /**
     * Calls the Open AI api and returns a Flowable of SSE for streaming.
     *
     * @param apiCall  The api call
     * @param emitDone If true the last message ([DONE]) is emitted
     */
    public Flowable<SSE> stream(Call<ResponseBody> apiCall, boolean emitDone) {
        return Flowable.create(emitter -> apiCall.enqueue(new ResponseBodyCallback(emitter, emitDone)), BackpressureStrategy.BUFFER);
    }


    /**
     * Retrieves a message based on the thread ID and message ID.
     *
     * @param threadId  The ID of the thread
     * @param messageId The ID of the message
     * @return The retrieved Message object
     */
    public AssistantMessage retrieveMessage(String threadId, String messageId, Long userId) {
        return execute(openAiApi.retrieveMessage(threadId, messageId, String.valueOf(userId)));
    }


    public File uploadAssistantFile(String filePath,
                                    String fileName,
                                    MediaType mediaType,
                                    Long userId) {
        java.io.File file = new java.io.File(filePath);
        RequestBody purposeBody = RequestBody.create(MultipartBody.FORM, "assistants");
        RequestBody fileBody = RequestBody.create(mediaType, file);
        MultipartBody.Part body = MultipartBody.Part.createFormData("file", fileName, fileBody);
        return execute(openAiApi.uploadFile(purposeBody, body, String.valueOf(userId)));
    }

    public ChatCompletionResult createChatCompletion(ChatCompletionRequest request) {
        return execute(openAiApi.createChatCompletion(request));
    }


    public Flowable<CompletionChunk> streamChatCompletion(ChatCompletionRequest request) {
        request.setStream(true);
        return stream(openAiApi.createChatCompletionStream(request), CompletionChunk.class);
    }


    public String createAssistantToken(Long userId, String llmBaseUrl, String llmApiKey) {
        if (StrUtil.isBlank(llmBaseUrl) || StrUtil.isBlank(llmApiKey)) {
            throw new BusinessException("The token parameter obtained by the assistant service is illegal");
        }
        if (llmBaseUrl.endsWith("/")) {
            llmBaseUrl = llmBaseUrl.substring(0, llmBaseUrl.length() - 1);
        }
        AssistantTokenRequest request = AssistantTokenRequest.builder()
                .llmBaseUrl(llmBaseUrl)
                .llmApiKey(llmApiKey)
                .description(StrUtil.format("SamePage user {} LLM Assistant Token", userId))
                .build();
        return execute(openAiApi.createAssistantToken(request)).getAssistantToken();
    }

    public void modifyAssistantToken(Long userId, String llmBaseUrl, String llmApiKey, String assistantToken) {
        if (StrUtil.isBlank(llmBaseUrl) || StrUtil.isBlank(llmApiKey) || StrUtil.isBlank(assistantToken)) {
            throw new BusinessException("The token parameter obtained by the assistant service is illegal");
        }
        if (llmBaseUrl.endsWith("/")) {
            llmBaseUrl = llmBaseUrl.substring(0, llmBaseUrl.length() - 1);
        }
        ModifyAssistantTokenRequest request = ModifyAssistantTokenRequest.builder()
                .description(StrUtil.format("SamePage user {} LLM Assistant Token", userId))
                .llmBaseUrl(llmBaseUrl)
                .llmApiKey(llmApiKey)
                .assistantToken(assistantToken)
                .build();
        execute(openAiApi.modifyAssistantToken(request));
    }
}
